							<p>Na reunião descentralizada dos diretores da Federação dos
								Trabalhadores em Saúde do RS – FEESSERS desta sexta-feira
								(24/06) foram apresentados os resultados da amostragem da
								situação dos hospitais filantrópicos do RS, realizada pela
								FEESSERS, e a campanha feita em conjunto com os SINDISAÚDES para
								alertar a opinião pública e os deputados gaúchos sobre a crise,
								o desemprego e o assédio moral.</p>

							<p>O documento “A Saúde Está à Beira do Abismo no RS –
								Desarticulação do SUS e Desgaste dos Hospitais Afeta Todos os
								Gaúchos” foi amplamente distribuído e atingiu seus objetivos de
								alerta da sociedade, entenderam os presentes, embora os
								representantes dos hospitais não tenham participado como havia
								sido previsto e combinado.</p>

							<p>O presidente da FEESSERS, Milton Kempfer, fez um apelo aos
								sindicatos no sentido de que os trabalhadores da saúde
								notifiquem às direções sobre o número de mortes decorrentes aos
								vírus da H1N1 e à incidência da caxumba. Para ele, a Secretaria
								Estadual da Saúde mascara os números.</p>

							<p>O diretor de Comunicação da Federação, Emerson Pacheco, trouxe
								os resultados da plenária aberta do Cofen, realizada no dia 23
								em Minas Gerais, onde foram debatidos políticas públicas,
								projetos de interesse da categoria e o ensino de Enfermagem no
								Brasil. Ele ressaltou que foi encaminhado um pedido de revisão
								do Código de Ética solicitando que no processo ético por erro de
								enfermagem sejam levados em conta número de horas trabalhadas,
								de pacientes atendidos e erros de rotulagem de medicamentos.</p>

							<p>No encontro com representantes de todas as regiões do estado e
								que contou com a presença de três dirigentes do SINDISAÚDE de
								Passo Fundo - Terezinha Perissinotto, Maria Tedesco e Marinês
								Pandolfo - ainda foram escolhidos os delegados que participarão
								em 17 e 18 de novembro na eleição da nova diretoria da CNTS.</p>

							</p>


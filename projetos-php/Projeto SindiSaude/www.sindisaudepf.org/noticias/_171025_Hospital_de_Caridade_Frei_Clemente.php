							<p>Pagamento dos salários em atraso e fechamento do acordo
								coletivo são as duas premissas básicas para o encerramento da
								greve dos trabalhadores em saúde do Hospital de Caridade Frei
								Clemente, que já dura 14 dias. Hoje (25/10), informou o
								presidente da instituição Sebastião Avani da Silva, se
								confirmado o pagamento dos 30% restantes do MAC - Média e Alta
								Complexidade Ambulatorial e Hospitalar, verba federal e os
								incentivos de Julho (verbas estaduais), deverá ser pago o
								salário de setembro. A promessa foi feita durante reunião na
								terça-feira (24/10) com a presença do diretor administrativo
								Adairto Ângelo Forti, do presidente da Federação dos
								Trabalhadores em Saúde do RS – FEESSERS, Milton Kempfer, da
								presidente do Sindisaúde Passo Fundo – Terezinha Perissinotto, o
								advogado Everton De Ré e diretoras do sindicato. Com a forte
								mobilização dos trabalhadores, em torno de 140, a administração
								mostrou-se disposta a fechar o acordo coletivo imediatamente,
								manter o adicional noturno em 45% para todos os funcionários e
								retomar o pagamento de insalubridade que havia sido retirado das
								folhas salariais.</p>

							<p>REFORMA TRABALHISTA</p>

							<p>Com a proximidade da entrada em vigor da Reforma Trabalhista,
								prevista para o dia 11 de novembro, a presidente Terezinha
								Perissinotto está preocupada com o futuro dos trabalhadores e o
								desmonte dos direitos adquiridos. Para Terezinha “é preciso
								fortalecer cada vez mais o sindicato, apostar no fortalecimento
								da categoria através da união de esforços e manter a mobilização
								sempre”. FEESSERS</p>

							<p>Sempre atuante, a FEESSERS mais uma vez se fez presente junto
								aos trabalhadores de Soledade, diz o presidente Milton “porque
								esta mesma situação está sendo vivenciada em Tupanciretã, no
								Hospital Brasilina Terra, também em greve, e em outras
								instituições onde ocorrem atrasos salariais, falta de pagamento
								de férias, 13º, FGTS e INSS”. Para ele, “ a grande
								responsabilidade sobre esta crise, cada vez mais intensa no
								estado, é do governador SARTORI, que não priorizou em momento
								algum a saúde, cortou os investimentos dos hospitais e
								investimentos na área, não cumpre sequer o determinado na CLT e
								arrasta os trabalhadores neste jogo, onde sabemos que dinheiro
								existe e certamente vai aparecer em período pré-eleitoral”. Veja
								nas fotos a tensão e expectativa dos trabalhadores em relação às
								conversas realizadas ao longo da tarde.</p>


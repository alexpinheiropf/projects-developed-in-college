								dos 12% mínimos da RLIT até chegar a 15% no prazo de seis anos.</p>

							<p>O presidente da Federação dos Trabalhadores da Saúde do RS
								(FEESSERS), Milton Kempfer, apresentou na noite da quarta-feira
								(22/03), na Sala Adão Preto da Assembleia Legislativa,
								importantes propostas para melhorar o financiamento e a gestão
								do SUS, “arrecadar mais e gastar melhor”.</p>

							<p>Na Audiência da Comissão Especial sobre a Sustentabilidade
								Financeira do SUS e a Participação dos Entes Federados, Milton
								defendeu, em primeiro lugar, que o Governo do Estado cumpra o
								Artigo 33 da Lei 8080, garantido que todos os recursos para a
								saúde sejam depositados numa conta especial. Segundo o dirigente
								“isso evitaria que os recursos do SUS fossem utilizados para
								outras prioridades do governo, atrasando repasses e prestadores
								e prefeituras, além de facilitar o controle social”.</p>

							<p>A segunda proposta sustentada por ele, também em nome dos
								SINDISAÚDES filiados,o fim das tabelas e das emendas
								parlamentares destinados ao setor. “Que todos os serviços,
								públicos e filantrópicos, sejam orçados. Que o custo de
								manutenção dos serviços sejam pagos por meio de repasses da
								união, estados e municípios e que aquele estabelecimento que
								atende convênios e privados, tenha descontado do repasse os
								valores arrecadados com a parte privada do custeio”, observou.</p>

							<p>A taxação das grandes fortunas foi outra das propostas da
								Federação apresentadas na audiência assim como “um intenso
								combate à sonegação”, pois segundo observou em sua explanação,
								de acordo com o Sindicato Nacional dos Procuradores da Fazenda
								Nacional (Sinprofaz), a sonegação no Brasil vem crescendo
								anualmente, só de 2013 para 2015 saltou de R$ 415 bilhões a R$
								420 bilhões.</p>

							<p>A volta da Contribuição sobre Movimentações Financeiras
								(CPMF), para ele, seria outra das fontes para sustentar o SUS,
								mas apenas a partir do salário mínimo calculado pelo
								Departamento Intersindical de Estatística e Estudos
								Socioeconômicos (DIEESE) para sustentar uma família de 04
								pessoas, que é de R$ 3.654,90. “ A partir daí uma aplicação
								progressiva de 0,20% até 0,40 % e rateio entre as esferas de
								governo, de acordo com a participação de cada uma no
								financiamento da saúde”, explicou.</p>

							<p>Por fim, Kermpfer ainda justificou a criação de uma Proposta
								de Emenda Constitucional (PEC) estadual semelhante a do estado
								de Santa Catarina que exigisse a aplicação dos 12 % mínimos da
								RLIT até chegar a 15 %, com a progressividade de 0,5 % ao ano,
								também foi defendida.</p>




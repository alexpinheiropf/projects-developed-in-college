						<nav id="noticia">
							<p>Acompanhadas pelo presidente da Federação dos Trabalhadores em
								Saúde (FEESSERS), Milton Kempfer, diretoras do SINDISAÚDE
								participaram na tarde de sexta-feira, 20/10, de audiência no
								salão do júri do Foro local.</p>

							<p>A audiência, realizada por iniciativa do juiz José Pedro
								Guimarães, da 2ª Vara Cível da Comarca de Soledade, que teve o
								objetivo de debater e buscar medidas de auxílio à situação do
								Hospital de Caridade Frei Clemente (HCFC), não trouxe nada de
								concreto. Apenas foram propostas metas a serem buscadas para a
								criação de fontes de receitas complementares com a realização de
								ações comunitárias para ajudar o hospital.</p>

							<p>Ao final da audiência foi formalizada uma comissão gestora
								comunitária paritária que terá a função de centralizar as ações,
								gestão dos recursos e acompanhamento de medidas de saneamento
								financeiro do HCFC.</p>

							<p>Também foram criadas as subcomissões de Agronegócio; Indústria
								e Comércio; Clubes de Serviços; Político (Avasb e Amasb) e
								Jurídico (Fórum, OAB, Ministério Público). Ainda ficou definido
								que haverá reuniões mensais deliberativas na terceira
								sexta-feira de cada mês, objetivando a adoção de medidas
								concretas de auxílio financeiro à casa de saúde, mediante
								eventos sociais e mobilização comunitárias.</p>

							<p>Ao manifestar-se, Milton Kempfer questionou a suposta crise
								financeira do estado que leva todo o setor da saúde a enfrentar
								dificuldades. Também propôs que as forças políticas da região e
								a direção da Federação, que representa os hospitais
								filantrópicos, devem cobrar do governo Sartori a volta do
								pagamento do Incentivo de Cofinanciamento da Assistência
								Hospitalar (IHOSP), criado no governo Tarso, e que complementava
								o custeio na assistência hospitalar com R$ 300 mil mensais.</p>

							<p>Participaram pelo SINDISAÚDE, Terezinha Perissinotto, Maria
								Tedesco, Ivanir Fornari Gomes, Maria Cristina Oliveira da Silva
								e Leila Maronezi.</p>


						<nav id="noticia">
							<p>Uma semana agitada para os técnicos em enfermagem do Hospital
								São Vicente, em Passo Fundo, resultou na decisão de entrarem em
								greve a partir desta segunda-feira, dia 27. Foram realizadas
								diversas reuniões e duas Assembleias na sede do Sindisaúde Passo
								Fundo, quando participaram mais de 300 profissionais que clamam
								por melhores condições de trabalho, por salários dignos e pela
								redução da jornada de trabalho.</p>

							<p>Preocupados com os rumos de uma possível greve no maior
								hospital da região, que atende 50 municípios, a direção da
								FEESSERS – Federação dos Trabalhadores em Saúde do RS e do
								Sindisaúde Passo Fundo entraram em contato com as autoridades
								municipais, estadual e federal em busca de apoio e de uma
								solução. Ao mesmo tempo, a direção do Hospital produziu e
								encaminhou documento ao sindicato alegando vários motivos pelos
								quais dizem desconhecer a motivação da greve neste momento.</p>

							<p>Em seu documento a direção diferencia, por exemplo,
								funcionários recém-chegados e outros de maior tempo de casa. O
								teor do documento causou revolta entre os profissionais e um
								aumento na adesão à greve. Entre outras questões, foram
								levantados os seguintes pontos:</p>

							<p>1. “Mesmo com data base em maio, a Associação Hospitalar
								resolveu antecipar um aumento salarial para seus funcionários já
								no mês de janeiro de 2014” seguindo orientação de pesquisa
								realizada internamente;</p>

							<p>2. “Assim, a Associação Hospitalar concedeu aumento de 19,65%
								para os técnicos de enfermagem que iniciam as suas atividades na
								empresa. O salário base era de R$ 1.028,00 e passou para $
								1.230,00”.</p>

							<p>3. “Após a conclusão de um ano de trabalho na empresa, os
								técnicos de enfermagem ganham automaticamente um aumento de
								salário base, que antes era de R$ 1.246,00 e agora passou para
								R$ 1.350,00, resultando em um aumento de 8,35%”. Ainda segundo
								eles, os técnicos estão recebendo um “dos maiores salários base
								da região”.</p>

							<p>4. “Para os demais trabalhadores da instituição, houve um
								aumento salarial de 7% também a título de antecipação de
								dissídio, ou seja, em percentual maior do que a inflação
								acumulada nos últimos 12 meses”.</p>

							<p>
								<strong>Comitê de Greve</strong>
							</p>

							<p>Paralelo, formou-se um Comitê de Greve, o qual ficou
								responsável por organizar os grevistas de forma estratégica a
								manter a greve sempre fortalecida e conquistando a adesão de
								novos colegas a cada dia. O grupo está reunido em caráter de
								urgência e de forma ininterrupta para atender a todos que
								desejam maiores esclarecimentos ou que sejam voluntários para as
								muitas atividades que serão necessárias a partir desta
								segunda-feira.</p>

							<p>À frente da organização a presidente do Sindisaúde PF
								Terezinha Perissinoto, o presidente da FEESSERS Milton Kempfer e
								demais diretores das entidades estão presentes em todos os
								movimentos, clamando à população de Passo Fundo e região que
								compreendam os motivos da greve e a importância de conquistar
								melhores condições de trabalho e de vida, que certamente
								resultarão em melhor qualidade de atendimento e melhores
								resultados para todos.</p>

							<p>
								<strong>Panfleto</strong>
							</p>

							<p>Na sexta, dia 24, foi publicado um A Pedido, voltado para os
								trabalhadores e população, informando sobre a greve e seus
								motivos. Da mesma forma, foi produzido um panfleto de
								esclarecimento para este mesmo público.</p>

							<p>
								<strong>Mobilização</strong>
							</p>

							<p>Importante ressaltar ainda, a forte mobilização dos
								profissionais que estão dialogando com todos seus colegas de
								trabalho, passando em todos os andares do hospital e explicando
								os motivos da greve. E, a grande demonstração do quanto o
								movimento está fortalecido, foi a caminhada realizada por mais
								de 100 técnicos, entre o hospital e o sindicato, onde ao final
								dos trabalhos do dia foi deflagrada a greve.</p>


							<p>Em evento de mobilização contra a falta de repasses do Governo
								Sartori, o presidente da Federação, Milton Kempfer, e a
								presidente do Sindicato, Terezinha Perissinotto, defenderam
								ontem (17/01) que todos os esforços regionais se dirijam no
								sentido de conseguir manter o Hospital de Caridade de Palmeira
								das Missões (HCPM).</p>

							<p>No evento, que lotou a Câmara de Vereadores, Milton afirmou
								que recursos existem, o que falta é a prioridade. “Se afirmam
								que existem R$ 60 milhões em uma conta para o futuro hospital
								regional e mais uma emenda de R$ 50 milhões, que eles sejam
								canalizados para o hospital que já existe”, afirmou ao fazer uso
								da tribuna.</p>

							<p>Terezinha enfatizou que as comunidades precisam ser
								mobilizadas na defesa do HCPM e dos trabalhadores da casa que
								atendem a toda a região e que enfrentam o atrsop no pagamento
								dos salários.</p>

							<p>Na ocasião, foi aprovada pela platéia a “Carta dos Hospitais à
								Comunidade”, que explicou as dificuldades enfrentadas pelas
								entidades e pelos trabalhadores para dar continuidade a seus
								serviços.</p>

							<p>Ficou decidido que será solicitada uma audiência, urgente, com
								o governador José Sartori, onde será solicitado um calendário de
								repasses dos R$ 24 milhões devidos aos hospitais das Regiões da
								Produção e Caminho das Águas (15ª,16ª e 19ª Coordenadoria
								Regional de Saúde) e que sejam regularizados os depósitos
								devidos.</p>

							<p>Compareceram ao evento prefeitos, vereadores, dirigentes
								hospitalares, funcionários e dirigentes de estabelecimentos de
								saúde além de membros das comunidades.</p>


							<p>A diretoria do Sindicato participou ativamente dos trabalhos
								nesta quinta-feira, 05/05, em Porto Alegre na Casa dos
								Capuchinhos, onde os diretores da FESSERS discutiram o
								planejamento para a nova gestão que tomaria posse à tarde.</p>

							<p>No dia de hoje, os debates giraram em torno da estrutura e das
								ações de cada pasta. De acordo com os dirigentes da Federação, o
								ano promete ser de muitas atividades, seguindo o parâmetro de
								2015 e do primeiro semestre, onde a saúde segue sem perspectivas
								positivas por parte do Governo Sartori. Também foram discutidas
								as indicações de nomes para composição da Chapa para a eleição
								da Confederação Nacional dos Trabalhadores em Saúde - CNTS, que
								ocorre em novembro.</p>

							<p>Ontem, a pauta ficou em torno das demandas sugeridas pelos
								Sindisaúdes, que indicaram temas a serem discutidos ao longo do
								período. Entre eles, a necessidade da campanha salarial
								unificada, reforço na formação sindical e projetos voltados para
								os aposentados. A organização contra a supresssão dos direitos
								trabalhistas e previdenciários, ativados pelo Golpe de Estado em
								andamento, e o reflexo para os trabalhadores do calote do
								Governo do Estado para os hospitais também foram debatidos.</p>

							<p>As mulheres trabalhadoras da saúde do Rio Grande do Sul, de
								várias regiões, reuniram-se para organizar suas reivindicações
								no espaço da entidade Nacional, a CNTS.</p>

							<p>Na tarde de hoje, a Comissão Eleitoral deu posse a nova
								Diretoria da Federação dos Empregados em Estabelecimentos e
								Serviços de Saúde do RS para o quadriênio 2016/2020,
								reconduzindo Milton Kempfer para a presidência. A presidenta do
								SINDISAÚDE Terezinha Perissinotto foi eleita representante da
								FEESSERS na Confederação Nacional dos Trabalhadores na Saúde e a
								Diretora de Finanças Maria Tedesco ficou na suplência junto à
								CNTS.</p>

							<p>Depois da posse um grupo de danças de cultura africana
								apresentou um show para os presentes.</p>


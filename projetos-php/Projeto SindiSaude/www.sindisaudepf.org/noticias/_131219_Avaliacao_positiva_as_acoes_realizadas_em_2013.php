							<p>No dia 19 de dezembro, o Sindisaúde de Passo Fundo promoveu a
								última reunião de diretoria de 2013, junto da realização da
								prestação de contas anual, do seminário de formação e da
								confraternização de final de ano.</p>

							<p>De acordo com Terezinha Perissinoto, presidente da entidade
								sindical, na ocasião foi realizada uma avaliação das ações
								executadas pelo Sindicato no último ano, além de um planejamento
								para 2014. “Foi um período de muito trabalho, e como
								consequência, fechamos 13 acordos coletivos na região. Apesar
								das dificuldades, temos uma avaliação positiva do ano que
								passou”, afirma.</p>

							<p>Em relação aos projetos para este ano, Perissinoto revela que
								o foco do Sindicato se manterá na implantação da jornada de
								trabalho de 30 horas para a categoria, sem a redução dos
								salários. No entanto, as ações também serão destinadas para a
								redução dos casos de assédio moral e a diminuição da evasão dos
								trabalhadores da saúde para outras áreas.</p>

							<p>Vanderlei de Oliveira Farias, diretor da Faculdade de Medicina
								da Universidade Federal da Fronteira Sul (UFFS) - Campus Passo
								Fundo, também participou da reunião, fazendo uma apresentação do
								curso ministrado na entidade. Para Perissinoto, a parceria foi
								uma boa oportunidade para mostrar que a categoria também tem
								sugestões para aprimorar a qualificação da formação médica.</p>

							<p>Além dos diretores do Sindicato, participaram da reunião,
								Milton Kempfer, presidente da FEESSERS, Emerson Pacheco,
								secretário geral da Federação e representante da CNTS e Jorge
								Gimenez Peralta, do Seapi.</p>

							<p>
								<strong>Conselho Comunitário</strong>
							</p>

							<p>A partir deste ano, a Feessers, por meio de Milton Kempfer,
								passa a compor o Conselho Comunitário da UFFS - Terezinha
								Perissinoto já integrava o grupo que é composto pela comunidade
								acadêmica e a sociedade civil organizada, tendo representação de
								diversas entidades como hospitais, prefeituras, movimentos
								sociais, entre outros.</p>

							<p>De acordo com a dirigente sindical, desta forma será possível
								discutir todas as questões inerentes à comunidade e à formação
								profissional, além de identificar, questionar e sugerir
								melhorias junto ao Campus.</p>


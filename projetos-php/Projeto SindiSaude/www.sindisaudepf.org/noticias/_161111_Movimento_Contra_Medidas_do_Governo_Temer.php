							<p>Dirigentes do SINDISAÚDE integraram a manifestação, iniciada
								às 5h da manhã, e passaram por diversos pontos da área central
								da cidade, somando cerca de dois mil participantes, entre outros
								sindicatos e o movimento social da cidade. Ao longo do trajeto,
								pontos importantes da malha viária tiveram o trânsito totalmente
								interrompido.</p>

							<p>Durante a realização dos atos públicos dentro do movimento, a
								presidente do Sindicato, Terezinha Perissinotto, a diretora de
								Finanças, Maria Tedesco, e o assessor jurídico, Everton de Ré,
								fizeram uso da palavra. Eles ressaltaram a importância da
								derrubada da Proposta de Emenda Constitucional (PEC) 241 – agora
								convertida em PEC 55 no Senado Federal – que congela por 20 anos
								os investimentos em áreas vitais como a saúde e a educação</p>

							<p>Defronte à Justiça do Trabalho, Éverton enfatizou que o
								objetivo do atual governo também passa pela perseguição desta
								instância judicial, vital para a defesa dos direitos dos
								trabalhadores do pais.</p>

							<p>Desde às 5h da manhã, integrantes da direção do Sindicato
								postaram-se diante dos portões de duas das maiores empresas de
								ônibus da cidade: CODEPAS e COLEURB, argumentando com os
								rodoviários para que se integrassem ao movimento até as 8h.
								Durante todo o dia, a Brigada Militar acompanhou as
								manifestações e, junto com a Guarda Municipal de Trânsito,
								garantiu a normalidade do movimento pacífico.</p>

							<p>Entidades representativas de diferentes categorias, entre elas
								agricultores, professores e trabalhadores em geral, movimentos
								sociais, estudantes e indígenas se manifestaram contra as
								medidas adotadas pelo governo federal nas áreas da saúde,
								educação, direitos trabalhistas e reforma previdenciária, e
								ainda relacionadas à exploração do petróleo no Pré-Sal.</p>

							<p>Manifestações também foram realizadas diante do Fórum Central
								e da Agência da Previdência Social, onde o movimento foi
								encerrado às 14h. O ato principal teve início na Praça Tochetto
							</p>

							<p>Reunidos ontem (16/02) em assembleia no inicio da tarde, os
								funcionários do Hospital de caridade decidiram não voltar a
								paralisar as suas atividades, mesmo tendo a receber o 13º
								salário de 2016, o salário de janeiro, as férias atrasadas e as
								diferenças de três meses do acordo coletivo. A decisão já foi
								comunicada à direção da instituição, em uma reunião ocorrida no
								hospital no meio da tarde, entre o presidente da FEESSERS,
								Milton Kempfer, a presidente do SINDISAUDE de Passo Fundo e
								Região, Terezinha Perissionotto, e a diretora do sindicato,
								Maria Tedesco, com dirigentes da instituição.</p>

							<p>A categoria levou em consideração que o hospital já apresenta
								um quadro bastante debilitado em sua capacidade de atendimento
								para a população, estando hoje com apenas dois setores em
								funcionamento.</p>

							<p>Esta razão fez com que optassem por não retomar o processo de
								paralisação de suas atividades neste momento, dando mais um
								prazo à instituição para quitar os seus pagamentos em atraso. Os
								trabalhadores, no entanto, solicitam à direção da casa uma
								reunião ampliada com a presença do prefeito Eduardo Russomano
								Freire, quando esperam obter uma posição da direção do hospital
								e do Executivo Municipal sobre como pretendem enfrentar essa
								situação, evitando o fechamento do hospital e trazendo
								tranqüilidade aos seus funcionários.</p>

							<p>O presidente do SINDISDAÚDE de Cruz Alta, Márcio Barboza, e a
								diretora de Imprensa, Beatriz Cezimbra, foram à Palmeira dar um
								apoio ao movimento.</p>

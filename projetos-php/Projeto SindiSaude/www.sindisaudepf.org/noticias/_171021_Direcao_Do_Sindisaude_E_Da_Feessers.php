							<p>Hoje pela manhã, a presidente do SINDISAÚDE, Terezinha
								Perissinotto, e o presidente da Federação dos Trabalhadores em
								Saúde do RS – FEESSERS, Milton Kempfer, reuniram-se com o
								administrador do Hospital Frei Clemente para buscar uma novidade
								em relação aos pagamentos devidos aos funcionários do Hospital,
								em greve desde o último dia 10.</p>

							<p>De acordo com Adairton Forti, hoje deverão ser pagas as férias
								de agosto e o resto dos salários em atraso de agosto graças ao
								pagamento de 70% da prestação de serviços que o governo do
								estado depositou para o hospital. Ele prometeu o salário de
								setembro para quando o governo quitar os 30% restantes, o que
								deve ocorrer até o final do mês.</p>

							<p>Terezinha e Milton enfatizaram a necessidade de que a direção
								da casa de saúde voltar a pagar a insalubridade dos
								trabalhadores, já tão penalizados com o atraso. Foi graças a
								insistência dessas entidades que o adicional noturno foi reposto
								às folhas de pagamento. São direitos adquiridos da categoria,
								que não podem ser retirados, argumenta a presidente do
								sindicato.</p>

							<p>A expectativa agora está na audiência que será realizada
								amanhã no Fórum da cidade, reunindo prefeituras da região para
								as quais o Frei Clemente presta serviços e dos quais a
								administração do HCFC espera um aumento nos repasses para dar um
								fôlego a administração, segundo Forte.</p>

							<p>Também participaram do encontro Maria Cristina Oliveira da
								Silva e Ivanir Formariam Gomes, funcionárias do Hospital e
								diretoras do SINDISAÚDE.</p>


							<p>Em matéria veiculada hoje no Jornal do Almoço regional, o
								presidente da Federação dos Trabalhadores da Saúde do RS, Milton
								Kempfer defendeu a adoção por parte do Governo do Estado de um
								calendário de repasses e a regularização mensal dos débitos para
								com os hospitais como forma de as folhas de pagamento ser
								mantidas em dia. “Não vemos nenhuma sinalização neste sentido,
								mesmo com todas as mobilizações que a FEESSERS e os Sindisaúdes
								tem realizado”, afirmou.</p>

							<p>Segundo Milton, alguns hospitais ainda devem meia folha de
								abril, mas outros devem mais. Com a crise, muitos trabalhadores
								estão adoecendo o que causa ainda mais problemas. “Neste
								período, quando há um aumento no volume de internações de
								crianças e idosos, isso causa um grande impacto no sistema”,
								observou.</p>

							<p>Um levantamento realizado pela RBS confirmou que, com três mil
								pacientes ao mês só na urgência/emergência e 180 funcionários, o
								Hospital Cristo Redentor de Marau tem para receber R$ 1,5 milhão
								entre Estado e União.</p>

							<p>m 125 funcionários, o São Paulo de Lagoa Vermelha tem um
								crédito de R$ 550 mil e também atende três mil pessoas na
								urgência. O Frei Clemente, de Soledade deve o mês de abril aos
								funcionários. O Hospital de Caridade de Carazinho tem R$ 1
								milhão a receber e as cirurgias eletivas estão suspensas desde
								abril, mesmo mês que deve de salários atrasados.</p>

							<p>O Santa Julia Billiart de Não Me Toque tem R$ 800 mil a
								receber e deve fechar a urgência/em emergência a partir de 23 de
								maio. Com um atraso de repasses de R$ 600 mil, o Comunitário de
								Sarandi também enfrenta a crise. Em Palmeira das Missões, o
								Hospital de Caridade também está com os salários atrasados e tem
								um débito de R$ 1,3 milhões a receber do Estado, anunciando que
								alguns serviços devem parar.</p>

							<p>Em Passo Fundo, o Hospital da Cidade teve parte dos repasses
								pagos pelo Governo do Estado, mas ainda tem acumulados R$ 10
								milhões em repasses não pagos. A direção admite que o corte nos
								serviços nos outros hospitais já representa 20% a mais nos
								atendimentos.</p>

							<p>Já o Hospital São Vicente de Paulo tem R$ 150 milhões por
								receber e os R$ 1,5 milhão/mês na retirada de valores no custeio
								devem resultar na redução de leitos e na qualidade assistencial.</p>


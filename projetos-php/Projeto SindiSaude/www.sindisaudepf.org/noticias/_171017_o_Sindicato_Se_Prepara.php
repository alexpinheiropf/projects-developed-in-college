							<p>O SINDICATO SE PREPARA PARA A ENTRADA EM VIGOR DA REFORMA
								TRABALHISTA Você está preparado para as mudanças que entram em
								vigor com a reforma trabalhista no dia 11 de Novembro? A sua
								central sindical, a sua federação e seu sindicato estão se
								preparando para protegê-lo. Mas “sabe como é que vai ficar? Pode
								ficar melhor se você se filiar ao sindicato. A responsabilidade
								de manter o sindicato e a tua sobrevivência como trabalhador
								amparado é tua”.</p>

							<p>Esta foi o recado dado aos trabalhadores da saúde, hoje
								(17/10), ao final do segundo dia do Encontro Estadual de
								Dirigentes e Trabalhadores em Saúde do RS pelo presidente da
								CUT-RS, Claudir Nespolo.</p>

							<p>Com o tema “Uma Leitura Sobre a Nova CLT e as
								Inconstitucionalidades da Lei”, o evento, iniciado ontem no
								salão da Casa de Eventos dos Capuchinhos, em Porto Alegre,
								trouxe excelentes informações, esclarecimentos importantes e um
								indicativo forte de resistência contra o desmonte que o Governo
								Temer vem aplicando nas normas e regulamentações das relações de
								trabalho.</p>

							<p>Nos dois dias, a presidente do SINDISAÚDE, TEREZINHA
								Perissinotto, e os diretores Jaime Steffens, Marissandra de Melo
								e Marinês Pandolfo, além do assessor jurídico Evérton de Ré,
								participaram de uma verdadeira capacitação, aulas mesmo,
								ministradas pelo procurador-chefe do Ministério Público do
								Trabalho no Rio Grande do Sul (MPT-RS), Rogério Uzun
								Fleischmann, a desembargadora presidente do Tribunal Regional do
								Trabalho da 4ª Região (RS) Beatriz Renck e a assessora Jurídica
								da Confederação Nacional dos Trabalhadores (CNTS), Zilmara David
								de Alencar.</p>

							<p>REFORMA É ESCRAVIDÃO</p>

							<p>Entre outros dirigentes sindicais, assessores jurídicos e
								convidados, eles tomaram conhecimento de conceitos como “a
								reforma trabalhista é uma forma de escravidão contemporânea”,
								como explicou Zilmara Alencar. Segundo ela, para sobreviver, o
								trabalhador estará disposto a fazer o que oferecerem a ele “em
								troca de uma mera sobrevivência”.</p>

							<p>Para a consultora, a reforma trabalhista viola convenções
								internacionais ratificadas pelo Brasil, como a Convenção 155 -
								que trata da saúde e segurança do trabalhador – e as que têm o
								objetivo de promover a negociação coletiva para a melhoria das
								condições de trabalho, para elas alcancem patamares superiores
								aos previstos na legislação - as convenções 98 e 154.</p>

							<p>Ainda de acordo com Zilmara, a nova lei fere também a
								Constituição Federal de 1988, que trouxe um patamar mínimo
								civilizatório, “garantindo um mínimo de dignidade humana”.</p>

							<p>Em sua palestra, ela declarou que a reforma do governo Temer
								não respeita sequer direitos adquiridos e desmonta todo cenário
								de proteção ao trabalhador, excluindo limites legais necessários
								“para um trabalho digno, decente e protetivo, não deixando
								dúvidas de que o trabalhador passará a ser tratado, de forma
								legal, como mercadoria, com a redução da proteção social e uma
								visível reformulação do direito coletivo do trabalho”, declarou.</p>

							<p>O procurador do MPT-RS apresentou situações contraditórias que
								poderão ocorrer com a entrada em vigor da reforma. Ele também
								demonstrou grande preocupação com as futuras relações
								contratuais, que vão retirar todos os direitos atuais. Para ele,
								o caminho será o fortalecimento dos sindicatos, sob pena de
								sucumbirem, “deixando os trabalhadores sem qualquer tipo de
								defesa”.</p>

							<p>A desembargadora presidente do TRT fez avaliação no mesmo
								sentido, afirmando que nas relações de trabalho, os brasileiros
								passarão a ser considerados “menos cidadãos” pois diversas
								alterações trazidas pela legislação aprovada não trarão os
								benefícios alegados e confrontarão a Constituição de 88.</p>


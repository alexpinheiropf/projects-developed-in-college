						<nav id="noticia">
							<p>A FEESSERS, por meio de suas diretoras e diretores e das
								diretoras e diretores dos SINDISAUDES FILIADOS, se posiciona
								radicalmente contrária à reforma previdenciária proposta pelo
								Governo TEMER por não concordar com as causas motivadoras
								apresentadas.</p>

							<p>Avaliamos que a Seguridade Social é superavitária em mais de
								R$ 20 bilhões e a proposta não apresentar o cálculo atuarial
								para os próximos 25 anos; por atacar conquistas de gênero;
								aumentar o tempo mínimo de contribuição; diminuir os salários
								dos aposentados e dos benefícios dos segurados e atingir somente
								os aposentados de menor salário.</p>

							<p>Por fim, entendemos que a proposta do governo não irá resolver
								o problema da Previdência Social e muito menos o problema
								orçamentário da União. Do nosso ponto de vista, a reforma só irá
								aumentar ainda mais a recessão e o abismo social entre os mais
								ricos e os mais pobres.</p>

							<p>Veja os argumentos do Governo TEMER com as principais mudanças
								propostas:</p>

							<p>
								<b>A reforma é necessária?</b>
							</p>

							<p>A Previdência registra um rombo crescente: gastos saltaram de
								0,3% do PIB, em 1997, para projetados 2,7%, em 2017. Em 2016, o
								déficit do INSS chega aos R$ 149,2 bilhões (2,3% do PIB) e, em
								2017 está estimado em R$ 181,2 bilhões. Os brasileiros estão
								vivendo mais, a população tende a ter mais idosos e o número de
								jovens que sustentam o regime tende a diminuir.</p>

							<p>
								<b>Quais são as principais mudanças?</b>
							</p>

							<p>O governo vai fixar idade mínima de 65 anos para requerer
								aposentadoria e elevar o tempo mínimo de contribuição de 15 anos
								para 25 anos.</p>

							<p>
								<b>Quem será afetado?</b>
							</p>

							<p>Todos os trabalhadores ativos. Quem tem menos de 50 anos terá
								de obedecer às novas regras integralmente. Quem tem 50 anos ou
								mais será enquadrado em normas um pouco mais suaves, mas com
								tempo adicional para requerer o benefício. Aposentados e aqueles
								que completarem os requisitos para pedir o benefício até a
								aprovação da reforma não serão afetados.</p>

							<p>
								<b>Quando as mudanças entrarão em vigor?</b>
							</p>

							<p>Vai depender da aprovação da reforma no Congresso.</p>

							<p>
								<b>Regras de transição</b>
							</p>

							<p>Haverá uma regra de transição para não prejudicar quem está
								perto da aposentadoria. Por ela, quem estiver com 50 anos ou
								mais (homens) e 45 anos ou mais (mulheres) poderá se aposentar
								pelas regras atuais, pagando pedágio de 50% sobre o tempo que
								faltava para a aposentadoria (se for um ano, por exemplo, terá
								de trabalhar um ano e meio).</p>

							<p>
								<b>Fórmula de cálculo do benefício</b>
							</p>

							<p>O governo pretende mexer na fórmula de cálculo e pressionar o
								trabalhador a contribuir por mais tempo e, assim, melhorar o
								valor do benefício. Hoje, dificilmente, o segurado recebe
								benefício integral. A idéia é aplicar um percentual de 51% sobre
								a média das contribuições, acrescida de um ponto percentual a
								cada ano adicional de contribuição.</p>

							<p>
								<b>A fórmula 85/95 vai acabar?</b>
							</p>

							<p>Essa fórmula tem previsão para durar até 2026, mas, com a
								reforma, vai acabar. Com a mudança, a aposentadoria
								exclusivamente por tempo de contribuição no setor privado vai
								acabar. Valerá a idade mínima de 65 anos, mais um tempo mínimo
								de contribuição de 25 anos.</p>

							<p>
								<b>O que muda para os funcionários públicos?</b>
							</p>

							<p>Já existe idade mínima no setor público, que é de 60 anos
								(homem) e 55 anos (mulher), mais tempo mínimo de contribuição de
								35 anos (homem) e 30 anos (mulher). Neste caso, a idade vai
								subir para 65 anos.</p>

							<p>
								<b>Diferença de regras entre homens e mulheres</b>
							</p>

							<p>Hoje, as mulheres podem se aposentar antes dos homens (com
								cinco anos a menos). O governo pretende unificar em 65 anos a
								idade mínima para os dois sexos. A nova regra afeta mulheres com
								até 45 anos. Acima desta idade, valerá a regra de transição.
								Assim, a igualdade ocorrerá gradualmente, ao longo de 20 anos.</p>

							<p>
								<b>Regimes especiais (professores, policiais militares e
									bombeiros)</b>
							</p>

							<p>A proposta inclui professores, que atualmente podem se
								aposentar cinco anos antes. Eles terão regras equiparadas aos
								demais trabalhadores. Para PMs e bombeiros, a idéia é fixar
								idade mínima. Atualmente, a categoria pode se aposentar só por
								tempo de contribuição.</p>

							<p>
								<b>Pensão</b>
							</p>

							<p>A pensão por morte, que é integral, deve ser reduzida para
								50%, mais 10% por dependente, para todos os segurados (INSS e
								serviço público). A pensão deverá ser desvinculada do reajuste
								do salário mínimo, que permite ganhos reais.</p>

							<p>
								<b>Trabalhadores rurais</b>
							</p>

							<p>Considerados segurados especiais, os trabalhadores das áreas
								rurais podem se aposentar por idade (60 anos homens e 55,
								mulheres), bastando apenas comprovação da atividade no campo. O
								governo quer que esse segmento também passe a contribuir para o
								regime, com alíquota semelhante à do MEI, de 5%. A idade sobe
								para 65 anos.</p>

							<p>
								<b>Benefícios assistenciais (LOAS)</b>
							</p>

							<p>Idosos ou deficientes de baixa renda têm direito a um
								benefício assistencial mesmo sem nunca terem contribuído. A
								idéia é desvincular este benefício da política de reajuste do
								salário mínimo, que permite ganhos reais. Os benefícios seriam
								reajustados só pela inflação. A idade deve subir de 65 anos para
								70 anos.</p>

							<p>
								<b>Desvinculação do piso da Previdência do salário mínimo</b>
							</p>

							<p>Diante da insegurança jurídica, o governo decidiu não
								desvincular o reajuste do salário mínimo do piso previdenciário
								(aposentadorias), o que exerce forte impacto nas contas do INSS.
								Essa mudança atingirá somente as pensões por morte e os
								benefícios assistenciais (Loas).</p>

							<p>
								<b>Fim da paridade entre servidores ativos e inativos</b>
							</p>

							<p>A regra atual assegura o mesmo reajuste salarial para todos e
								na mesma data. A novidade afetaria todos que ingressaram no
								serviço público antes de 2003 e ainda não se aposentaram. Esses
								trabalhadores passariam a ter direito só à reposição da inflação
								no momento de reajustar o benefício. O mecanismo já vigora para
								quem entrou depois de 2003.</p>

							<p>
								<b>Alíquota de contribuição para a Previdência</b>
							</p>

							<p>O governo pretende elevar de 11% para 14% a alíquota de
								contribuição dos funcionários públicos federais — que funciona
								como piso para os regimes próprios estaduais, a pedido dos
								governadores.</p>

							<p>
								<b>Militares das Forças Armadas</b>
							</p>

							<p>Os militares ficarão de fora. A idéia é alterar as regras
								desses servidores em projeto à parte. O governo pretende elevar
								o tempo de serviço para pedir transferência para a reserva de 30
								para 35 anos; aumentar a contribuição, hoje em 7,5%, para
								equiparar à dos funcionários públicos; e a idade limite para
								ficar na ativa deve acabar.</p>


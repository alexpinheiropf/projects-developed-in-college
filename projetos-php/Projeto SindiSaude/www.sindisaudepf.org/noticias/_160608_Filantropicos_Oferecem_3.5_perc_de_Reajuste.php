							<p>
								<strong>Sindicato marca assembleia para avaliar a proposta.</strong>
							</p>

							<p>O lamentável reajuste de 3,5% oferecido hoje pela manhã pelos
								representantes do Sindicato dos Hospitais Beneficentes,
								Religiosos e Filantrópicos do Rio Grande do Sul indignou
								dirigentes do SINDISAÚDE de Passo Fundo. Segundo a presidente da
								entidade, Terezinha Perissinotto a proposta, que representa um
								terço do INPC - índice que mede a inflação - (9,81%) e do Piso
								Regional (9,612%), está muito longe dos 15% encaminhados pelos
								trabalhadores.</p>

							<p>Na terceira reunião de negociação realizada no Hospital da
								Cidade entre as duas partes também houve a tentativa de
								retrocesso nas cláusulas econômicas e sociais como o adicional
								noturno, a insalubridade sobre o Piso Regional, creche e redução
								de jornada.</p>

							<p>Neste quadro, o Sindicato decidiu marcar para quarta-feira, 08
								de junho, uma assembleia para que os funcionários dos hospitais
								filantrópicos discutam a proposta patronal.</p>

							<p>Na reunião, Terezinha afirmou não concordar com o reajuste
								argumentando que os salários dos trabalhadores da saúde são
								muito baixos. “Reconhecemos que os hospitais não estão recebendo
								o que o governo estadual lhes deve, mas não fomos nós que
								criamos esta situação”, observou.</p>

							<p>Os representantes dos hospitais continuam alegando a crise
								financeira, o corte de programas e atraso de repasses para
								oferecer a minguada proposta, mas para o presidente da Federação
								dos Trabalhadores da Saúde do RS os hospitais não devem ceder ao
								Governo do Estado.</p>

							<p>“Se nós cedermos, a cada ano os recursos vão escassear ainda
								mais e, em consequência, a situação dos trabalhadores ficará
								cada vez pior. Não podemos nos conformar com a situação porque
								ainda temos três anos de governo pela frente”, enfatizou Milton
								Kempfer, presente ao encontro.</p>


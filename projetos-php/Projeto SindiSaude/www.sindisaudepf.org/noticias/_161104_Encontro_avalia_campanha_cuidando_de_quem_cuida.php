							<p>O diretor presidente da FEESSERS, Milton Kempfer, e a
								presidente do SINDISAÚDE de Passo Fundo, Terezinha Perissinotto,
								reuniram-se ontem (31/10) pela manhã, na sede do Sindicato em
								Passo Fundo, com a professora Vanderléia Pulga, Coordenadora
								Adjunta de Extensão e Cultura do Campus Passo Fundo da
								Universidade Federal da Fronteira Sul (UFFS), para fazer uma
								avaliação preliminar dos eventos de lançamento da Campanha
								CUIDANDO DE QUEM CUIDA.</p>

							<p>O presidente da FEESSERS relatou que foram oito as atividades
								de lançamento da Campanha realizadas até agora: No dia 07/10 em
								Passo Fundo realizou-se um evento estadual com a presença de
								dirigentes dos 27 sindicatos filiados; no dia 08/10 foi a vez
								das cidades de Santiago e São Borja; 17/10 – Alegrete; 21/10 –
								Caxias do Sul; 26/10 – Bagé; 27/10 – Pelotas e 04/11 - São
								Gabriel.</p>

							<p>Na ocasião, Milton apresentou dados de uma rápida enquete
								realizada por quatro SINDISAÚDES durante panfleteação da
								Campanha em Passo Fundo, Bagé, Caxias do Sul e Santiago.</p>

							<p>A campanha, cujo slogan “CUIDA MELHOR DO OUTRO, QUEM CUIDA
								MELHOR DE SI”, e que reúne o Outubro Rosa e o Novembro Azul,
								deve ter um novo encontro entre os parceiros locais – Hospital
								da Cidade de Passo Fundo e Hospital São Vicente de Paulo e
								Secretaria Municipal da Saúde a ser agendado para a próxima
								semana, quando também se integra o diretor do Campus Passo Fundo
								da Universidade Federal da Fronteira Sul (UFFS), Vanderlei de
								Oliveira Farias, atualmente em férias.</p>

							<p>
								<b>ENQUETE MOSTRA ALTO ÍNDICE DE ROTATIVIDADE DA CATEGORIA </b>
							</p>

							<p>Dados de uma rápida enquete realizada por quatro SINDISAÚDES
								durante panflete ação da Campanha em Passo Fundo, Bagé, Caxias
								do Sul e Santiago apontam para um alto índice de rotatividade
								entre os trabalhadores dos hospitais pois 45,96% possui só até
								cinco na função.</p>

							<p>De cinco a dez anos encontramos 19,61%; de dez a 15 (14,61%);
								de 15 a 20 (6,92%); de 20 a 30 (10,19%) e com mais de 30 anos na
								função foram encontrados apenas 2,69%. Dos 532 entrevistados
								(46% mulheres e 13,53%, homens), nesta faixa de idade,
								detectamos pessoas com 30, 33, 35, 37, e até 40 anos na mesma
								função.</p>

							<p>Outro dado que ressalta na enquete é que apenas 20,75% dos
								entrevistados relataram afastamentos por doença contra 79,24%. É
								importante, porém, afirmar que as pessoas em situação de
								afastamento atual não fizeram parte do levantamento.</p>

							<p>Por faixa etária, podemos afirmar que a maioria dos
								trabalhadores da saúde que responderam aos questionários é
								considerado jovem pois 61,78% tem abaixo dos 40 anos de idade
								(27,37% de 20 a 30 anos e 34,41% de 30 a 40 anos) contra 38,20%
								que possui acima dos 40 anos (24,90% entre 40 e 50 e 13,30%
								entre 50 e 70 anos).</p>

							<p><b>(*) Atenção, os dados deste levantamento não possui nenhum
								fundamento científico. É fruto apenas de uma amostragem
								aleatória.</b></p>


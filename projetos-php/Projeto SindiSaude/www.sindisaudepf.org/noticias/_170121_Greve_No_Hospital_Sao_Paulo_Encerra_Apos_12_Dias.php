							<p>Depois de 12 dias paralisados, os funcionários do Hospital São
								Paulo decidiram acabar com o movimento na noite de sexta-feira
								20/01. A direção do SINDISAÚDE e a direção da instituição
								firmaram um acordo de que até na sexta-feira, dia 27, serão
								pagos os 40% restantes do salário de dezembro.</p>

							<p>Terezinha Perissinotto, que preside o Sindicato de Passo Fundo
								e Região, considera que o saldo do movimento paredista resultou
								em um avanço significativo porque, mesmo com toda a angústia e
								preocupação com a situação dos trabalhadores, se evoluiu para um
								respeito mútuo entre as duas partes.</p>

							<p>“Nesses 12 dias, foram pagos a última parcela do 13º salário,
								quitados os salários de novembro e pagos 60% dos salários de
								dezembro”, observa, alertando, no entanto que caso não seja
								cumprido o acordo a greve pode ser retomada na próxima semana.</p>

							<p>Ela ficou impressionada positivamente com a compreensão da
								população da Lagoa Vermelha, que contribuiu com a doação de
								cestas básicas para os trabalhadores em greve. Segundo ela,
								igrejas, pessoas individualmente e até familiares de pacientes
								internados fizeram doação de alimentos. “Todos nos acolheram de
								braços abertos e solidários”, observou. Terezinha ainda ressalta
								que a imprensa local foi parceira em todos os momentos,
								divulgando o movimento.</p>

							<p>A dirigente afirmou ainda que o SINDISAÚDE será parceiro na
								busca de mais recursos para o São Paulo e melhores condições de
								trabalho para os seus funcionários na comissão especial que será
								criada pela Câmara de Vereadores para acompanhar o trabalho de
								reerguê-lo.</p>


								além do 13° Salário. O caminho pode ser a greve, como observa
								Milton Kempfer, presidente da Federação dos Empregados em
								Estabelecimentos e Serviços da Saúde do RS (FEESSERS). Segundo
								ele, a situação vem sendo ocasionada pela falta de repasse dos
								incentivos dos meses de outubro e de novembro e pelo atraso no
								pagamento dos serviços prestados por santas casas e hospitais
								filantrópicos.</p>

							<p>Na região abrangida pelo SINDISAÚDE de Passo Fundo, o hospital
								de Caridade de Palmeira das Missões deve o mês de novembro e 50%
								do 13º Salário. O Frei Clemente de Soledade deve 60% de novembro
								e todo o 13º.</p>

							<p>“Já tivemos 19 greves em 2017 por falta de pagamento.
								Atualmente temos paralisação em Três Passos e Canguçu, mas o
								movimento paredista poderá se estender por várias
								regiões”,observa o dirigente.</p>

							<p>Segundo ele, a categoria, que já possui baixos salários,
								jornadas excessivas e sobrecarga de trabalho, acaba alcançando
								um alto índice de adoecimento e de estresse. “Contas atrasadas,
								corte de luz e despejo tem sido o dia-a-dia de quem atende a
								população em seu momento de maior vulnerabilidade. Isto é
								extremamente perigoso e quem acaba sofrendo é a população”,
								observa Milton.</p>

							<p>A situação mais grave ocorre na Beneficência Portuguesa de
								Porto Alegre, onde os trabalhadores estão há seis meses sem
								receber os salários, mas Milton adverte que os primeiros a ser
								atingidos com uma possível onda de paralisações poderão ser os
								moradores das regiões de Arroio Grande, Boa Vista do Buricá,
								Cacequi, Canela, Canguçu, Caxias do Sul (Fundação Araucária),
								Campo Novo, Coronel Bicaco, Crissiumal, Cruz Alta, Bagé, Boa

						<nav id="noticia">
							<p>
								Um evento mágico reuniu hoje (06/10), de maneira inédita,
								sindicatos dos trabalhadores e administradores de hospitais lado
								a lado em Passo Fundo (RS). O lançamento da Campanha <b>CUIDANDO
									DE QUEM CUIDA</b> trouxe à sede do SINDISAÚDE de Passo Fundo
								dirigentes do Hospital da Cidade e do Hospital São Vicente de
								Paulo que entraram na campanha como apoiadores. O objetivo está
								no slogan <b>“CUIDA MELHOR DO OUTRO, QUEM CUIDA BEM DE SI”</b>.
							</p>

							<p>Ela vem no sentido de alertar trabalhadores e trabalhadoras da
								saúde dos hospitais e casas de saúde da região abrangida pelo
								Sindicato sobre a sua importância no mundo da saúde. Que, antes
								de cuidarem dos pacientes os profissionais tenham a consciência
								de que a sua saúde e a de seu colega deve vir em primeiro lugar.
								A solidariedade é o ponto alto da campanha, ela deve existir
								entre todos nas equipes, no sentido de observar a si e aos seus
								colegas.</p>

							<p>A Universidade Federal da Fronteira Sul (UFFS) - Campus Passo
								Fundo, a Federação dos Empregados em Estabelecimentos de Saúde
								do Estado do RS (FEESSERS) e seus 27 SINDISAÚDES filiados
								estarão promovendo a campanha - neste mês e em novembro. Ela foi
								concebida pelo Campus Passo Fundo da UFFS, com seu curso de
								medicina, e pelo SINDISAÚDE de Passo Fundo e agora vai se
								estender para todo o estado, agregando o Outubro Rosa e o
								Novembro Azul.</p>

							<p>Em Passo Fundo, a Secretaria Municipal da Saúde integrou-se ao
								movimento com a promessa de oferecer a coleta de materiais para
								os exames de cólo de útero nos duas maiores instituições – o
								Hospital da Cidade (HC) e o Hospital São Vicente de Paulo
								(HSVP).</p>

							<p>O Conselho Regional de Enfermagem (COREN-RS) e o Sindicato dos
								Enfermeiros do RS (SERGS) também apoiam a iniciativa, cujo
								lançamento da campanha já tem várias agendas pelo interior do
								Estado: São Borja e Santiago, nesta sexta-feira, 07/10. Caxias
								do Sul, 21/10 e Pelotas, 28/10.</p>

							<p>Participaram do evento nesta tarde o secretário municipal da
								Saúde, Luis Arthur Rosa Filho; o presidente da Confederação
								Nacional dos Trabalhadores em Seguridade Social (CNTSS), Sandro
								César; o tesoureiro-geral da Confederação Nacional dos
								Trabalhadores na Saúde (CNTS), Adair Vassoler e o
								diretor-presidente da FEESSERS, Milton Kempfer.</p>

							<p>Pelo HC, compareceram o seu administrador, Luciney Bohrer,
								Dionísio Balvedi e Olmiro Baggio - integrantes da diretoria-,
								Mário Amaral (RH) e a enfermeira Marisa Carretta Diniz. Também
								estiveram presentes o administrador do HSVP, Ilário Jandir De
								David, o gerente de recursos humanos, Jairo Davoglio e a gerente
								de Marketing, Fernanda Marini.</p>

							<p>Os presidentes do COREN-RS, Daniel Souza, e do SERGS, Estevão
								Finger da Costa prestigiaram o lançamento que ainda contou com a
								presença de dirigentes dos SINDISAÚDES de Pelotas, Ijuí, Santa
								Rosa, Caxias do Sul, Rio Grande, Erechim, Montenegro, Santiago,
								São Gabriel, Bagé e Vale dos Sinos.</p>

							<p>Na ocasião, foi apresentado um vídeo com a fala do reitor da
								UFFS, Dr. Jaime Giolo, representado no evento pelo diretor do
								Campus Passo Fundo da Universidade, Vanderlei de Oliveira
								Farias, e pela professora Vanderleira Pulga, Coordenadora
								Adjunta de Extensão e Cultura.</p>

							<p>O professor Júlio Cesar Stobbe , coordenador do Curso de
								Medicina da UFFS e vice-diretor médico do HSVP fez uma palestra,
								falado da importância da preservação de vínculos afetivos fortes
								para preservar a saúde das pessoas.</p>

							<p>Cultura foi o que não faltou: a apresentação da peça teatral
								Faixa de Graça do Grupo Ritornelo de Teatro, descontraiu o
								público, envolvendo os participantes que lotaram o Salão de
								Eventos da recém inaugurada sede do SINDISAÚDE de Passo Fundo.</p>


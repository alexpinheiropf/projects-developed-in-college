							<p>Pelo termo, hospital se compromete a fazer 102 obrigações em
								prazos que variam até 18 meses; ajuste de conduta resulta de
								operação da força-tarefa de adequação das condições de saúde e
								segurança no trabalho em hospitais no RS; HSVP também pagará
								multa de R$ 700 mil por violar jornada de trabalho; valor foi
								revertido para segurança pública.</p>

							<p>A Associação Hospitalar Beneficente Hospital São Vicente de
								Paulo (HSVP), em Passo Fundo, firmou, nesta terça-feira (10/10),
								termo de ajuste de conduta (TAC) com o Ministério Público do
								Trabalho (MPT). Durante audiência administrativa realizada na
								sede do MPT passo-fundense, a empresa comprometeu a fazer 102
								obrigações, envolvendo 4 de meio ambiente do trabalho, 7 de
								proteção de máquinas, 2 de caldeiras e vasos de pressão, 1 do
								trabalho em altura, 38 de aspectos ergonômicos, 32 de programas
								e medidas de proteção, 3 de condições sanitárias e de vivências
								nos locais de trabalho, 11 de Comissão Interna de Prevenção de
								Acidentes (CIPA), 2 de investigações de acidentes de trabalho e
								2 de disposições gerais. O prazo para cumprimento das cláusulas
								variam de imediatamente até 18 meses.</p>

							<p>O descumprimento injustificado de qualquer das obrigações
								pactuadas, entendidas como violação a qualquer cláusula,
								parágrafo, alínea, item ou subitem, ensejará aplicação de multa
								de R$ 5 mil, a cada constatação de descumprimento, devidamente
								atualizados, reversível Fundo de Amparo ao Trabalhador (FAT) ou
								à entidade beneficente a ser posteriormente definida. O TAC de
								13 páginas resultada da sexta operação da força-tarefa de
								adequação das condições de saúde e segurança no trabalho em
								hospitais no Rio Grande do Sul, realizada de 4 a 7 de abril,
								quando o HSVP foi notificado pelo MPT sobrre 190 irregularidades
								encontradas em 17 conjuntos de setores e atividades. Durante a
								tramitação do inquérito civil (IC), o Hospital comprovou a
								realização de 60 adequações.</p>

							<p>A procuradora do Trabalho Priscila Dibi Schvarcz informa que
								também foi ajustado com o HSVP o pagamento da multa de R$ 700
								mil, em decorrência da constatação da violação de normas
								referentes à jornada de trabalho, por força do acordo judicial
								celebrado anteriormente nos autos da ação civil pública (ACP)
								0001515-45.2010.5.04.0661. O valor foi revertido para a
								implementação de projetos devidamente protocolados no MPT,
								relacionados à segurança pública no Município de Passo Fundo. A
								Delegacia da Polícia Federal de Passo Fundo receberá R$ 75 mil,
								para implementação do projeto "Construção de Depósito de Drogas,
								Materiais Apreendidos e Dossiês". E o Conselho Comunitário
								Pró-Segurança Pública (Consepro) de Passo Fundo ganhará R$ 625
								mil para implementação do projeto "Guardião, Fases 2-A, 3 e 4".</p>


							<p>Em plenária extraordinária de mobilização, realizada na manhã
								desta sexta-feira (10/11), um dia antes do início da entrada em
								vigor da lei 13.467/2007, a chamada reforma trabalhista do
								governo Temer, a CUT-RS reafirmou que haverá resistência e
								enfrentamento para defender os direitos da classe trabalhadora e
								evitar retrocessos.</p>

							<p>Presente ao encontro, a presidente do SINDISAÚDE de Passo
								Fundo Terezinha Perissinotto lembrou as duas greves recentemente
								realizadas em Soledade e Palmeiras das Missões, onde as mulheres
								mostraram toda a garra e a força da trabalhadora da saúde, que
								foram dormir na barraca mesmo com seus filhos. Ela também
								exemplificou a resistência contra a reforma trabalhista por
								parte de uma funcionária do hospital Frei Clemente que, sozinha,
								conseguiu coletar 500 assinaturas na lista de apoio ao Projeto
								de Lei de Iniciativa Popular para revogar a 13.467. “Esta é a
								diferença”, explicou: “ a garra e a vontade de lutar das
								mulheres”.</p>

							<p>O encontro ocorreu no salão da igreja da Pompeia, em Porto
								Alegre, com cerca de 200 dirigentes sindicais de diferentes
								categorias e vindos de várias regiões do Rio Grande do Sul. Mais
								tarde, às 16h, eles participaram do abraço simbólico à Justiça
								do Trabalho, em frente ao prédio do TRT-RS, seguindo depois em
								caminhada até a Esquina Democrática para o ato unificado das
								centrais sindicais, às 18h, contra as “reformas” trabalhista e
								da Previdência e o trabalho escravo, em defesa da Justiça do
								Trabalho e por nenhum direito a menos.</p>

							<p>O presidente da CUT-RS, Claudir Nespolo, destacou que o que
								está em curso não é só um desmonte das leis trabalhistas, mas um
								violento desmonte civilizatório que impedirá retomar outros
								direitos. Ele defendeu o fortalecimento dos comitês de
								resistência nos municípios, a continuidade do recolhimento das
								assinaturas para o projeto de lei de iniciativa popular pela
								anulação da reforma trabalhista e da lei da terceirização sem
								limites, e a atuação conjunta com partidos de esquerda e
								movimentos sociais e populares para resistir cada vez mais aos
								ataques dos golpistas.</p>

							<p>Projeto de lei de iniciativa popular pela anulação da reforma
								trabalhista Foi também apresentada a situação da coleta de
								assinaturas no projeto de lei de iniciativa popular (PLIP) pela
								anulação da reforma trabalhista e da lei da terceirização sem
								limites.</p>

							<p>Já foram obtidas milhares de adesões no Rio Grande do Sul,
								porém a campanha vai continuar, uma vez que ainda não foi
								atingido o total de no mínimo 1,3 milhão de assinaturas em pelo
								menos cinco estados do país, conforme estabelece a Constituição.</p>


							<p>Está marcada para o dia 31 de maio a terceira reunião do
								SINDISAÚDE com o Sindicato dos Hospitais Beneficentes,
								Religiosos e Filantrópicos do Rio Grande do Sul – SINDIBERF, sem
								que a patronal tenha apresentado nenhuma proposta para o Acordo
								Coletivo de 2016. Embora o SINDISAÚDE tenha pedido 15% de
								reajuste salarial, os 11 representantes dos hospitais, que ontem
								sentaram à mesa de negociações no Hospital da Cidade em Passo
								Fundo não acenaram com nenhum percentual.</p>

							<p>Alegando a grave a crise financeira, o corte de programas e
								atraso de repasses do Governo do Estado, não convenceram os seis
								diretores do Sindicato que representavam os trabalhadores e o
								presidente da FEESSERS. “É impossível não termos uma proposta de
								reajuste em função do processo inflacionário em curso, que já
								alcança a casa dos 10%”, declarou Milton Kempfer.</p>

							<p>Ele disse que a Federação dos Trabalhadores em Saúde é
								parceira para buscar recursos para os hospitais, mas quanto à
								questão dos reajustes não pode concordar. “Desde que veio o
								anúncio da crise financeira do Governo Sartori e o da União, os
								trabalhadores da saúde tem sido penalizados., Não podemos nos
								conformar com a situação porque ainda temos três anos de governo
								pela frente”, enfatizou.</p>

							<p>A presidente do SINDISAÚDE afirmou que a expectativa “era de
								que sairíamos daqui com uma proposta. Os trabalhadores não podem
								pagar uma conta que não fizeram”, declarou. “Temos dois
								objetivos: o primeiro, de continuarmos a nos manifestar contra o
								corte de recursos para a saúde do Governo Sartori e o segundo de
								não chegar a uma assembleia de trabalhadores em ter nenhuma
								proposta dos hospitais.”</p>


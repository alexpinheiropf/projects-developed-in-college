						<nav id="noticia">
							<p>Na manhã da quarta-feira (11/01) a presidente do SINDISAÚDE de
								Passo Fundo, Terezinha Perissinotto, participou no Hospital São
								Paulo (HSP) de uma reunião entre prefeitos de Lagoa Vermelha,
								Caseiros, Ibiraiaras, Capão Bonito do Sul e de representantes da
								Fundação Araucária, vereadores e secretários da saúde e técnicos
								da área, além de vereadores de Lagoa e funcionários do HSP.
								Terezinha enfatizou no encontro que os trabalhadores só voltarão
								ao trabalho quando tiverem os salários em atraso totalmente
								pagos. Segundo ela, a preocupação é que a Folha de Pagamento de
								cerca de R$ 300 mil mensais para os 118 funcionários não seja
								complementada às quatro parcelas de R$ 116 mil do governo do
								Estado para a Araucária.</p>

							<p>Entre os encaminhamentos acertados no encontro estão o empenho
								dos prefeitos na questão política para fazer com que o Estado
								repasse o deve à Fundação e que a mesma pague os funcionários
								com as verbas esperadas. Também se decidiu que seja constituído
								um conselho administrativo com dois representantes de cada uma
								das prefeituras e da sociedade civil de Lagoa Vermelha,
								Caseiros, Ibiraiaras e Capão Bonito do Sul, além de
								representantes da comissão dos vereadores de Lagoa Vermelha,
								Ministério Público, Fundação Araucária e Hospital São Paulo.</p>

							<p>Entre outras sugestões que surgiram, a de que o hospital possa
								ter outras formas de financiamento através da prestação de
								outros serviços, que hoje são comprados pelas prefeituras dos
								outros municípios, como exames e consultas. Isto poderia
								facilitar o transporte dos pacientes e auxiliaria o Hospital a
								ter outras formas de cobrir seus custos.</p>

							<p>Os municípios responsabilizaram-se em organizar e especificar
								qual a demanda de cada um. O hospital responsabilizou-se em
								divulgar quais os serviços tem disponibilidade em realizar e
								criar uma tabela de valores para a compra desses serviços.</p>

							<p>Os representantes irão reunir-se novamente na quarta-feira,
								dia 18/01, para apresentar os levantamentos e discutir com o
								Hospital São Paulo o que será resolvido.</p>

							<p>Com fotos da Assessoria de Comunicação da Prefeitura Municipal
								de Lagoa Vermelha.</p>


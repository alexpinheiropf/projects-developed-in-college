							<p>FEESSERS foi elogiada pela forte atuação que vem realizando “É
								inaceitável o que estão fazendo com a saúde do estado, mas é
								inadmissível o que estão fazendo com os trabalhadores, jogados
								ao descaso, sem saúde física ou mental em função dos atrasos
								salariais, parcelamentos, não pagamento, nome “sujo” na praça,
								falta de crédito, falta de condições mínimas de sobrevivência e
								da própria família e ainda tomam conhecimento de altos salários
								que alguns administradores e profissionais considerados
								especiais recebem.” O desabafo é de Milton Kempfer que
								representa mais de 120 trabalhadores da saúde do estado.</p>

							<p>As diretoras do SINDISAÚDE de Passo Fundo, Ivanir Fornari
								Gomes, Marisandra de Melo e Maria Tedesco compareceram ao
								encontro, em que esta indignação ocorreu, no final da agitada
								manhã de ontem (29/08) para os dirigentes da Federação dos
								Trabalhadores em Saúde do RS – FEESSERS e Sindisaúdes filiados
								participando de audiência com a vice-presidente da Assembleia
								Legislativa deputada estadual Liziane Bayne – deputado Edegar
								Pretto não pode comparecer em função de problemas de saúde na
								família. A audiência foi intermediada pelo deputado Tarcisio
								Zimmermann e contou com a participação dos também deputados
								Altermir Tortelli – presidente da Comissão de Saúde e Meio
								Ambiente e de Nelsinho Metalúrgico. Representando os
								trabalhadores, o presidente da FEESSERS Milton Kempfer
								apresentou um documento contendo um resumo da situação da saúde
								no estado e uma amostragem do que está ocorrendo nos hospitais
								que enfrentam maior dificuldade nas regiões.</p>

							<p>Fundamental para a manutenção do movimento foi o apoio da
								população, que ajudou os trabalhadores e suas famílias com o
								fornecimento de cestas básicas e alimentos não perecíveis,
								segundo a presidente do Sindicato, Terezinha Perissinotto.</p>

							<p>Esta é uma situação estrutural de desfinanciamento, apontou o
								deputado Tarcisio, que saudou o trabalho que a federação vem
								realizando, afirmando que “tem sido um trabalho muito importante
								e fundamental na luta pela saúde, não poupando esforços em busca
								de soluções e de melhores condições de salário e vida para os
								trabalhadores e também na ajuda ao financiamento dos hospitais”.</p>

							<p>CPI E FORÇA TAREFA</p>

							<p>Após tomar ciência do quadro penoso dos hospitais e
								principalmente dos trabalhadores, com relatos de fome e falta de
								crédito no mercado, corte de luz, aluguel atrasado entre outras
								situações dramáticas, o deputado Altemir Tortelli propôs a
								criação de uma CPI da Saúde. A idéia é abrir as contas,
								acompanhar a entrada do dinheiro no caixa do estado e roteiro
								que segue até o caixa dos hospitais e a decorrente aplicação do
								mesmo. Avaliar o ciclo todo. Além da CPI também será formada uma
								Força Tarefa com intuito de conseguir uma agenda com o
								governador Sartori, que embora muitas tentativas, se recusa a
								receber os trabalhadores.</p>

							<p>TRABALHADORES BANCAM O SISTEMA</p>

							<p>Uma conclusão foi unânime, o sistema de saúde do estado está
								entrando em total colapso. Mas, como sempre, quem está bancando
								o atendimento do Sistema Único de Saúde – SUS são os
								trabalhadores.</p>

							<p>Participaram também os Sindisaúdes de São Borja, Caxias do
								Sul, Santiago, Cruz Alta, Pelotas e Santana do Livramento.
								Também vieram de Livramento a presidente da câmara vereadora
								Maria Helena Duarte e os vereadores Aquiles Pires e Márcia da
								Rosa e os dirigentes de Tupaciretã, Palmeira das Missões e Santa
								Bárbara. Pela federação os diretores Carlos Weber e Antonio
								Sérgio da Silva acompanharam os trabalhos. E, os presidentes dos
								Sindisaúdes Pedro Salaberry – São Borja, Bianca D’Carla –
								Pelotas, José Airton Clérice – Santiago, Silvio Vidart Madruga –
								Livramento.</p>


							<p>A terceirização, aprovada na quarta-feira (22/03) na Câmara
								dos Deputados, acabou sendo o principal assunto explanado na
								Assembléia de Lançamento da Campanha Salarial Unificada para
								Passo Fundo e Erechim, realizada ontem (23/03) no auditório do
								SINDISAÚDE de Passo Fundo.</p>

							<p>“O que deve ser feito no pais é uma reforma de privilégios e
								não a trabalhista”, observou o presidente da FEESSERS, Milton
								Kempfer, para quem a possibilidade de terceirização na área
								hospitalar não irá atingir apenas os trabalhadores, mas que vai
								prejudicar também a população pela precarização no atendimento.</p>

							<p>Segundo a presidente do sindicato passo-fundense, Terezinha
								Perissinotto, a medida “foi uma paulada que lhe tirou o sono à
								noite”. O texto-base do projeto de lei que autoriza o trabalho
								terceirizado para qualquer tipo de atividade, que seguirá agora
								para sanção presidencial, também foi tema do presidente do
								SINIDAÚDE de Erechim, Adilson Szymanski, para quem vamos voltar
								ao tempo da escravatura.</p>

							<p>A diretoria de Assuntos Jurídicos do Sindicato dos Enfermeiros
								do RS, Claudia Ribeiro Franco conclamou a todos a participarem
								de uma greve geral no dia 1º de Maio, pois a terceirização “está
								rasgando os direitos dos trabalhadores”.</p>

							<p>Para o presidente do Sindicato dos Trabalhadores em Indústrias
								de Máquinas Agrícolas de Passo Fundo, Alcedir de Andrade, a
								terceirização no setor da saúde vai fazer com que os hospitais
								“passem a ser só a casca dos hospitais”. Ele entende que os seus
								funcionários poderão passar a ser todos terceirizados com a
								medida do Governo Temer.</p>

							<p>Presente ao encontro, o deputado estadual Altemir Tortelli
								(PT) também ressaltou que a aprovação do projeto é o “pagamento
								da fatura” à elite empresarial brasileira pelo apoio concedido
								ao “golpe” contra a presidenta Dilma Rousseff. Para ele, os
								únicos favorecidos com a sua aprovação serão os empresários, que
								poderão agora subcontratar seus empregados, via terceirização,
								sem se preocupar em assegurar direitos como férias, 13º salário,
								licença-maternidade, licença-saúde, respeitar a jornada dos
								trabalhadores e nem garantir segurança no ambiente laboral.</p>

							<p>DIEESE</p>

							<p>A palestra do diretor do Departamento Intersindical de
								Estatística e Estudos Socioeconômicos (DIEESE), Ricardo Franzói,
								apresentou os estudos sobre a região apontando que existem cerca
								de 74 mil técnicos e auxiliares de enfermagem na região. Este
								contingente, entre as 25 categorias profissionais existentes,
								ocupa o quinto lugar, mas em Passo Fundo é o segundo, atrás
								apenas dos comerciários.</p>

							<p>A remuneração média – entre salários, adicionais e 13º Salário
								-, no entanto está em nono lugar em Passo Fundo. Em Erechim,
								ocupa o 8º lugar em número de profissionais e o 11º em ganhos.</p>

							<p>A média salarial é de R$ 1.627,00, isto quer dizer, segundo o
								economista, que um quarto do seu salário serve para pagar a
								cesta básica, calculada em R$ 435,00. Ele sublinhou o fato de
								que, segundo o DIEESE, para sustentar uma família de quatro
								integrantes, o salário mínimo deveria ser de R$ 3.658,00.</p>


							<p>Uma campanha salarial unificada e participativa com consulta à
								categoria sobre os pontos a serem colocados em pauta pelos
								SINDISAÚDES nas negociações com a patronal. Este foi o principal
								ponto decidido pelos diretores da Federação dos Trabalhadores em
								Saúde do RS - FEESSERS de todas as regiões que participaram
								hoje, das 8h30min às 17h, do Seminário Campanha Salarial 2017.</p>

							<p>No encontro, ocorrido em Porto Alegre, na sede da Federação,
								também foi acordado que será formada uma comissão, composta,
								entre outros dirigentes pela presidente do SINDISAÚDE Passo
								Fundo, Terezinha Perissinotto, para já no dia 08 de dezembro
								iniciar o planejamento da campanha que será lançada em sete
								regiões que abrangem os 27 sindicatos filiados, cuja agenda
								também será definida no novo encontro.</p>

							<p>Ao abrir o seminário, pela manhã, o presidente da FEESSERS,
								Milton Kempfer falou sobre a atual conjuntura e as dificuldades
								que os trabalhadores em saúde estão enfrentando em todas as
								regiões, que incluem atrasos salariais, demissões, não pagamento
								de direitos como férias e 13º Salário, falta de qualidade de
								trabalho, entre outros fatores que já vem sendo denunciados.</p>

							<p>Em sua explanação, o supervisor técnico do Departamento
								Intersindical de Estatística e Estudos Sócioeconômicos (DIEESE),
								Ricardo Franzói, falou sobre a Proposta de Emenda Constitucional
								(PEC) 241/55, que visa limitar o crescimento da despesa da União
								por 20 anos. Ele enfatizou o desmonte do Estado e a Plataforma
								de Transferência de renda para o Setor Financeiro que está sendo
								empreendido pelo Governo Temer.</p>
							<p>
							
							
							<p>O assessor especial da CUT-RS, João Marcelo Pereira dos
								Santos, coordenou o trabalho de traçar os cenários político,
								econômico e social para a próxima Campanha Salarial, do ponto de
								vista dos dirigentes dos SINDISAÚDES, ajudando a traçar os
								próximos passos para a construção da campanha.</p>


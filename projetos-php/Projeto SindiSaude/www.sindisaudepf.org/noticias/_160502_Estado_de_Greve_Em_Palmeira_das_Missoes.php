							<p>Esta foi a proposta tirada na assembleia realizada no início
								da tarde de ontem (03/05). Eles também decidiram realizar uma
								grande mobilização na terça-feira, dia 10, para alertar a
								população quan...to à situação por que passam, com contas
								atrasadas e dificuldades financeiras.</p>

							<p>Antes da assembleia a presidente Terezinha Perissinotto, os
								diretores Maria Tedesco e Ozïel M. de Avïlla, acompanhadas pelo
								presidente da FEESSERS, Milton Kempfer e de funcionários
								reuniram-se com a direção da instituição, representantes da
								comissão intervencionista e o prefeito Eduardo Freire.</p>

							<p>
								<strong>Mobilização</strong>
							</p>

							<p>No dia 10, a partir das 10h, o Sindicato vai montar um
								acampamento na Praça Getúlio Vargas, recebendo os funcionários
								do hospital e a população para esclarecer os motivos do
								movimento. No início da tarde, às 13h, será feito um abraço ao
								HC seguido de uma passeata pela cidade. Às 19h de segunda-feira,
								dia 9, o Sindicato fará uso da Tribuna Popular na Câmara de
								Vereadores.</p>

							<p>Terezinha lembra que em agosto de 2015 houve um movimento
								semelhante envolvendo o Hospital de Caridade, quando um grande
								ato público jå manifestava a inconformidade com a falta dos
								repasses do Piratini, o atraso de salários e más condições de
								trabalho. Segundo ela, o atraso nos salários também está
								ocorrendo em outros estabelecimentos da base do SINDISAÚDE como
								o Hospital Frei Clemente de Soledade, o São Paulo de Lagoa
								Vermelha e Caridade de Carazinho.</p>


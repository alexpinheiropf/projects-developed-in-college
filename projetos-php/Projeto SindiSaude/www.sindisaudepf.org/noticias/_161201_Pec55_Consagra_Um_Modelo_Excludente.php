								CNTSS-CUT em Atibaia/SP está muito interessante, segundo o
								presidente da Federação dos Trabalhadores em Saúde do RS –
								FEESSERS. Para ele, a palestra do professor da UNICAMP e diretor
								da CESIT – Centro de Estudos Sindicais e de Economia do Trabalho
								- Denis Maracci foi excelente, porque retrata com precisão a
								realidade que estamos vivendo.</p>

							<p>Em sua fala, o professor Denis disse que “o que se gasta com o
								povo, o que se arrecada com o povo não tem déficit, mas os
								governos tem usado recurso da Seguridade Social para fazer
								superávit primário, desvinculando recursos” e que a “PEC55
								consagra um modelo excludente, projeta problemas pra frente e,
								não tem impacto imediato sobre os problemas imediatos da
								economia”.</p>

							<p>Conforme disse o dirigente no evento que é acompanhado peça
								Para Houve um ajuste virtuoso, a expansão de contribuintes e de
								receitas de 2004 até 2012, o governo ajustou a previdência sem
								cortar gastos, mas aumentando benefícios, amparados pelo
								crescimento econômico, comentou Denis. Observou ainda, que 68%
								da população vivia abaixo da linha da pobreza na década de 70 e
								isso evoluiu positivamente, mas em 2015 aconteceu o desastre da
								economia brasileira. Para o professor, nem tivemos esse quadro
								“nem em 1929 e tivemos na história a depressão que estamos
								vivendo agora e que poderá ser de 10% em dois anos”.</p>

							<p>Para o presidente da FEESSERS Milton Kempfer, estamos nesta
								situação, porque mentiram para a população que a inflação estava
								fora de controle, transformaram dificuldades na economia em
								depressão, inventaram uma crise que não havia e que foi
								transformada em queda de receita, colocando os estados e
								municípios em situação de insolvência.</p>


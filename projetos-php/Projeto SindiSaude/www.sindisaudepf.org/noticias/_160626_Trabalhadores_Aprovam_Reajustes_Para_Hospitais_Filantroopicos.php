							<p>Este e a internacionalização dos investimentos em saúde no
								Brasil foram os temas debatidos em São Paulo nos dias 21 e 22 de
								junho no Seminário Nacional Setor Saúde.</p>

							<p>Organizado pela ISP – Internacional de Serviços Públicos e a
								CNTSS/CUT – Confederação Nacional dos Trabalhadores em
								Seguridade Social teve a participação pelo Rio Grande do Sul da
								presidente do Sindicato, Terezinha Perissinotto e do presidente
								da Federação dos Trabalhadores da Saúde, Milton Kempfer e o
								presidente do SINDISAÚDE de Erechim, Adilson Szymanski.</p>

							<p>O principal objetivo do seminário foi o de avaliar os desafios
								que a nova realidade brasileira na saúde nos traz e ver como
								fortalecer a organização dos trabalhadores para enfrentar essa
								situação. “Além da difícil conjuntura política em geral, ainda
								se nota o forte crescimento do setor privado internacional da
								saúde no Brasil, que traz mudanças nas relações de trabalho e
								forte impacto no futuro do setor”, avaliou Jocelio Drummond,
								secretário regional interamericano da ISP.</p>

							<p>
								<strong>Perda de Verbas Na Saúde</strong>
							</p>

							<p>Entre os debatedores, a presença do ex-ministro da saúde,
								Arthur Chioro, que apresentou uma análise do ingresso do capital
								privado no setor saúde no Brasil: um balanço inicial e
								perspectivas de regulamentação.</p>

							<p>Chioro revelou que o Governo Interino de Michel Temer limitou
								os gastos no Ministério da Saúde em R$ 91,5 bilhões. Também
								contou que se a CPMF não tivesse sido derrubada hoje teríamos o
								equivalente a R$ 80 bilhões a mais para aplicar no setor. Outro
								alerta de perda de verbas é com relação a aplicação da Proposta
								de Emenda à Constituição PEC 143/2015 do Senado Federal que
								resultará em uma previsão de menos R$ 44 a R$ 65 bilhões entre
								União, Estados e Municípios.</p>

							<p>Por outro lado, outra PEC, a 241/2016, do novo ministro da
								Fazenda, Henrique Meirelles, também limita os gastos das esferas
								de governo no gasto do ano anterior mais o INPC, não importando
								a receita.</p>

							<p>A ISP é uma federação sindical internacional - sindicato
								global - para os/as trabalhadores/as de serviços públicos que
								agrupa 635 sindicatos filiados em 156 países.</p>

							<p>O seminário que ainda analisou os possíveis ataques aos
								direitos sindicais contou com dirigentes da Confederação
								Nacional dos Trabalhadores na Saúde (CNTS), entre outros.</p>

							<p>As lideranças sindicais apresentaram as demandas e
								encaminhamentos das entidades para enfrentamento de questões
								relacionadas à terceirização, precarização dos serviços de saúde
								dentre outras bandeiras que unificam as organizações sindicais
								em defesa da valorização profissional e ainda pela defesa
								intransigente do sistema único de saúde 100% público e de
								qualidade</p>

							<p>Também foi realizado um grupo de trabalho sobre “Propriedade
								Intelectual da REBRIP”, sob a coordenação de Pedro Villardi, da
								ABIA/REBRIP. Na sequência foi apresentado por Gabriel Casnatti,
								ISP Brasil, um Mapa do ingresso do capital privado na Saúde no
								Brasil. Também foram apresentadas campanhas pelo direito à
								saúde, sob a coordenação de Alessandra Nilo (GESTOS).</p>


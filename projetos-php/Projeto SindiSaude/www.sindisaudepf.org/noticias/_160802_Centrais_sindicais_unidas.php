							<p>Cerca de 1.200 trabalhadores reunidos no CTG Lalau Miranda, em
								Passo Fundo hoje (29/07) pela manhã, assistiram a união das
								centrais sindicais contra o desmonte do Ministério da
								Previdência Social, as mudanças na CLT e a extinção da Justiça
								do Trabalho. À tarde, uma passeata pelas principais ruas da
								cidade encaminhou-se das imediações da Câmara Municipal, no
								Bairro Annes, até à Rua General Osório e defronte ao prédio da
								Previdência Social foi realizado um ato público com centenas de
								trabalhadores e inflamados discursos de representantes das
								principais centrais sindicais.</p>

							<p>O presidente da CUT-RS fez um apelo aos trabalhadores do campo
								e da cidade para que se mobilizem em torno da realização de uma
								greve geral contra o desmonte da Previdência Social e a
								terceirização do trabalho. “Voltem para as suas bases e contem
								para seus colegas que mais que uma semana de trabalho ou meia
								dúzia de pés de milho plantados o que está em jogo é o futuro
								dos trabalhadores”.</p>

							<p>Claudir Nespollo foi um dos palestrantes na Audiência Pública
								Contra a Reforma da Previdência, Mudanças na CLT e a extinção da
								Justiça do Trabalho, organizada pela CUT Regional e que contou
								com a presença do senador Paulo Paim - presidente da Comissão de
								Direitos Humanos e Legislação Participativa do Senado Federal.</p>

							<p>Setembrino Dal Bosco, da CSP Conlutas, teceu um paralelo entre
								o governo interino de Michel Temer, em Brasília, e o do José
								Sartori, no Rio Grande do Sul, que, para ele, tomam medidas que
								confrontam os direitos dos trabalhadores com seus ajustes
								fiscais.</p>

							<p>A secretária estadual de mulheres da CTB-RS, Lenir Fanton,
								gritou palavras de ordem: “Não vamos deixar rasgar a CLT e a
								Constituição Federal”. E a coordenadora estadual da Fetraf,
								Cleonice Bach, fez um alerta de que as mulheres trabalhadoras
								rurais, pela reforma em curso do governo Temer, terão que
								trabalhar dez anos a mais e daqui a cinco anos mulheres e homens
								do campo estarão ganhando meio salário mínimo pela desvinculação
								dos reajustes das aposentadorias.</p>

							<p>O senador Paulo Paim apontou como três das principais ameaças,
								as mudanças na idade para aposentadoria – especialmente para as
								mulheres, que passariam a se aposentar com 65 anos -, a
								desvinculação entre o salário mínimo e o reajuste da
								aposentadoria do regime geral. Também discursaram os deputados
								federais Elvino Bohn Gass e Maria do Rosário, além dos deputados
								estaduais Altemir Tortelli, presidente da Frente Parlamentar a
								favor da Previdência no RS e Juliano Rosso.</p>


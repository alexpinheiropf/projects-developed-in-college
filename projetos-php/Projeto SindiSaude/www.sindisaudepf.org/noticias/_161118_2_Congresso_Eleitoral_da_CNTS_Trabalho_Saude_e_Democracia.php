							<p>Eleição em congresso amplia participação e responsabilidade da
								base da CNTS</p>

							<p>Ampliar a participação e compartilhar responsabilidades com as
								entidades da base é o objetivo central da eleição dos dirigentes
								da CNTS, prevista para 18 de novembro próximo, com chapa única e
								pela segunda vez em congresso. O pleito acontece junto com as
								demais atividades de comemoração dos 25 anos de trajetória da
								Confederação, que representa mais cerca de 3 milhões de
								trabalhadores. A eleição, antes resumida a um voto por federação
								filiada, reunirá cerca de 70 delegados votantes.</p>

							<p>A formação da chapa única, segundo o secretário-geral da CNTS
								o coordenador do processo, Valdirlei Castagna, foi fruto de
								acerto político discutido junto às federações. A comissão
								eleitoral, composta por Valdemir Guimarães, Carlos Santos e
								Raimundo Morais, avaliou e aprovou as etapas do processo. Ao
								todo, compõem a nova chapa um grupo de 48 pessoas, que
								coordenarão os trabalhos da Confederação no quinquênio de
								dezembro de 2017 a dezembro de 2022.</p>

							<p>A criação de novas diretorias vem atender a atualização do
								estatuto da CNTS, aprovado em agosto de 2015. “O novo estatuto
								representa um grande avanço para a CNTS, pois amplia a
								participação dos delegados representantes dos trabalhadores da
								saúde em todo o território nacional. Além disso, aos 25 anos de
								existência, é muito importante realizar as devidas atualizações
								para responder aos avanços e às mudanças de uma sociedade cada
								vez mais dinâmica e complexa”, ressalta o presidente da CNTS,
								José Lião de Almeida</p>

							<p>As mudanças tiveram como finalidade contemplar as atuais
								necessidades de maior participação das federações filiadas, além
								de promover alterações na estrutura da Confederação. Entre as
								principais mudanças está a ampliação do colegiado das atuais 16
								para 21 diretorias, visando atender as demandas frente ao
								crescimento da CNTS. “As modificações são um avanço no sentido
								de ampliar o espaço da CNTS para as federações”, avalia o
								tesoureiro da CNTS e coordenador do grupo de trabalho da reforma
								estatutária, Adair Vassoler.</p>

							<p>A nova composição da diretoria não terá os cargos de primeiro
								e segundo vice-presidentes. E foi reduzido o número de cargos
								adjuntos e criadas nove diretorias, entre elas a de políticas
								públicas e serviços públicos; assuntos de aposentados e
								pensionistas; negociação coletiva; imprensa e comunicação;
								segurança e saúde do trabalhador; pesquisa, arquivo e memória
								sindical; cultura, esporte e lazer; assuntos econômicos; e
								assuntos de gênero, raça, diversidade e juventude – esta última
								oriunda dos comitês especiais da CNTS.</p>

							<p>Congresso eleitoral - O congresso acontecerá em dois dias. “A
								CNTS está inovando na forma de eleição dos dirigentes.
								Realizaremos nos dias 17 e 18 de novembro o congresso eleitoral.
								No primeiro dia serão realizadas palestras sobre temas
								relevantes para os dirigentes sindicais da categoria e no dia 18
								acontece o processo de votação. Poucas entidades de terceiro
								grau realizam suas eleições por forma de congresso”, disse
								Castagna.</p>

							<p>Ainda segundo o secretário-geral, com este modelo, a CNTS
								amplia a participação das federações na escolha democrática dos
								dirigentes da Confederação. “O processo eleitoral acontecia via
								conselho de representantes, ou seja, cada entidade tinha direito
								a indicar um eleitor, envolvendo poucas pessoas na escolha dos
								gestores da entidade. A eleição via congresso é um processo
								muito mais democrático e nos dá condições, a partir dessa
								responsabilidade maior, de fazermos uma discussão cada vez mais
								ampla e próxima com as federações e sindicatos”.</p>

							<p>Os delegados votantes foram indicados pelas respectivas
								federações. “A indicação dos delegados é de extrema importância,
								pois através dela é que se forma o processo eleitoral. Esta é
								uma fase importante na vida da Confederação”, concluiu o
								secretário.</p>

							<p>Durante o congresso, serão encerradas as comemorações dos 25
								anos da CNTS, com realização de palestras sobre Conjuntura
								Política e Sindical; palestras sobre Violência Doméstica -
								Convivendo com o Agressor, tema da campanha realizada pela
								Confederação em 2016; realização de teatro interativo sobre
								Violência Doméstica; premiação ao vencedor do concurso para
								escolha do hino da Confederação; lançamento de livro
								comemorativo do Jubileu de Prata; e homenagem àqueles que
								ajudaram e/ou ajudam a construir a CNTS.</p>


							<p>Funcionários do Hospital de Caridade de Palmeira das Missões
								realizaram hoje à tarde uma caminhada pelo centro da cidade para
								denunciar à população a crise por eles enfrentada, oficializando
								também seu Estado de Greve.</p>

							<p>A manifestação - que teve o objetivo de esclarecer a situação
								à população – foi decidida em assembleia dos trabalhadores
								realizada no dia 03 de maio. Naquela ocasião, eles deram um
								prazo até o dia 15 de maio à direção do hospital para que pague
								os 70% que restam do salário de março e o de abril na
								integralidade.</p>

							<p>Para a presidente do SINDISAÚDE, Terezinha Perissinotto, é
								necessária a adesão da população e das entidades públicas e
								privadas em defesa dos trabalhadores, seus empregos e da
								manutenção dos próprios atendimentos. Ela enfatiza que o Estado
								de Greve é um alerta de que, a qualquer momento, os atendimentos
								podem ser paralisados.</p>

							<p>"A situação, que vem se repetindo ano a ano, mesmo antes da
								intervenção do Executivo Municipal, agravou-se de forma
								contundente no Governo Sartori com o corte de verbas para a
								saúde e o atraso nos repasses", observa Terezinha. Ela diz que a
								categoria entende a crise financeira da instituição, "mas não
								podemos deixar que os seus funcionários e os pacientes paguem
								esta conta. Para isto, estamos expondo nas ruas a grave
								situação".</p>

							<p>Para o presidente da FEESSERS, Milton Kempfer, está cada vez
								mais complicada a situação da saúde no estado, sem que haja
								qualquer sinalização por parte do governo Sartori em torno de
								uma solução. A Paciência se esgotou, disse ele, se referindo ao
								escárnio do secretário da saúde João Gabbardo, para o qual
								fechamento de hospitais e atrasos dos repasses vão continuar
								ocorrendo. E ainda debochou pedindo "paciência", disse Milton. A
								resposta será o aumento das mobilizações, afiançou. Ele e o
								secretário geral da entidade, Lino Puhl, acompanham o movimento
								em Palmeira das Missões.</p>

							<p>Na noite da segunda-feira, 09/05, ela e outros diretores do
								Sindicato ocuparam a Tribuna Popular da Câmara, pedindo apoio
								por parte dos vereadores, que demonstraram apoio às
								reivindicações do Sindicato. Em decorrência do atraso de
								repasses da União e do Governo do Estado, a Mesa Diretora propôs
								uma Moção de Apelo ao governador José Ivo Sartori (PMDB),
								visando a viabilização dos recursos atrasados.</p>


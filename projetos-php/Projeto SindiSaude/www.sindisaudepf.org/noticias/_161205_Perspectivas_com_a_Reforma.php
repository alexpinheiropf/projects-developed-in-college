							<p>O assessor jurídico do SINDISAÚDE de Passo Fundo, Everton de
								Ré, participou no dia 02/12 do evento Previdência: Perspectivas
								com a Reforma na sede da Ajuris. No encontro, que contou com o
								apoio da Federação dos Trabalhadores em Saúde do RS – FEESSERS
								foi privilegiado o debate sobre a reforma da Previdência e as
								consequências para os trabalhadores.</p>

							<p>Foram palestrantes no evento os advogados João Lucas de Mattos
								- especialista em Direito Previdenciário; Tiago Beck Kidrici
								também dedicado ao Direito Previdenciário e vice -presidente da
								Comissão especial de Previdência Social da OAB RS; Maria Isabel
								Pereira da Costa graduada em Ciências Jurídicas e Sociais e
								Paulo André Solano especialista em Direito Previdenciário.</p>

							<p>Para João Lucas Mattos, o maior problema da reforma no Brasil
								é a falta de transparência do processo. Também especialista no
								tema, Tiago Kidrici diz que a inexistência de um projeto
								apresentado com clareza, dificulta o debate, uma vez que não
								sendo possível barrar a reforma, é necessário tratar
								pontualmente cada caso. Maria Isabel da Costa acredita que é
								preciso garantir uma auditoria na Previdência para que a
								realidade dos números seja conhecida e Paulo Solano afirmou que
								o interesse fiscal motiva essas reformas e todas são para piorar
								e trazer prejuízo aos previdenciários.</p>

							<p>Com texto de Inara Claro.</p>


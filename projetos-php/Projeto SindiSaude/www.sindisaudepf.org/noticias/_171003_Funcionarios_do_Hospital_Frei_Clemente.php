							<p>Se até segunda, dia 09 de outubro, não receberem os 60¢
								restante do salário de agosto e o salário de setembro podem
								paralisar atividades.</p>

							<p>Reunidos em assembleia no início da noite de hoje (02/10), os
								trabalhadores do Hospital de Caridade Frei Clemente (HCFC)
								decidiram entrar em ESTADO DE GREVE a partir de hoje, dia 03 de
								outubro. Caso os 60% restantes do pagamento do mês de agosto e o
								mês de setembro não sejam pagos na integralidade até
								segunda-feira, dia 09, será realizada uma nova assembleia para
								examinar a possibilidade de paralisação. Na assembleia,
								acompanhada pelo presidente da Federação dos Trabalhadores da
								Saúde do RS, Milton Kempfer, também ficou decidido que o
								SINDISAÚDE - que representa a categoria - vai encaminhar um
								documento à direção do Frei Clemente, notificando sobre a
								decisão e buscando negociar a forma de pagamento dos salários
								atrasados. No documento também será enfatizada a necessidade de
								manutenção dos benefícios conquistados pelos trabalhadores com
								relação aos adicionais noturno e de insalubridade, que passaram
								a ser retirados, acarretando mais prejuízo financeiro.</p>

							<p>A presidente do SINDISAÚDE, Terezinha Perissinotto, afirma que
								será encaminhado o aviso legal no prazo legal de 72 horas para o
								Judiciário e às prefeituras do Soledade, Barros Cassal, Fontoura
								Xavier, Ibirapuitã, Mormaço e Tio Hugo, que mantém convênio com
								o HCFC.</p>

							<p>O presidente da FEESSERS disse entender que o problema do HCFC
								é de receita, o que poderia ser solucionado, em parte, com o
								aumento da participação do município de Soledade, que contribui
								com menos de R$ 100 mil. “para sustentar uma UPA, por exemplo, a
								prefeitura gastaria em torno de R$ 500 mil. Dessa forma, vale a
								pena repassar um pouco mais para manter o hospital”. De acordo
								com a própria direção do hospital, o maior problema é o atraso
								nos repasses do Governo do Estado, que deve R$ 270 mil para o
								Frei Clemente, mas Milton entende que o hospital já deveria ter
								cobrado os incentivos devidos por via judicial.</p>

							<p>Terezinha também reconhece a crise enfrentada pela casa, mas
								diz que para o SINDISAÚDE a prioridade são os trabalhadores. Ela
								relata a “situação de miserabilidade” da categoria porque os
								atrasos no pagamento dos salários vêm ocorrendo desde o início
								do ano. “Isto vem trazendo sérios transtornos para os
								funcionários, muitos escolhendo a conta que vai pagar primeiro”,
								ela explica, enfatizando que a população desconhece a situação
								de “verdadeira penúria” que eles enfrentam.</p>

							<p>A assembleia realizou-se em dois horários no Salão Azul da
								antiga Câmara de Vereadores.</p>


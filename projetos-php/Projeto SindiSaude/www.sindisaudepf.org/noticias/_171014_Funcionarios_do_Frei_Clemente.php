							<p>Na próxima segunda-feira, dia 16/10, os funcionários do
								Hospital de Caridade Frei Clemente de Soledade (HCFC), realizam
								uma caminhada pelo Centro daquela cidade. Em greve há cinco
								dias, eles aguardam o pagamento integral do salário de setembro
								e também os salários de agosto de alguns trabalhadores que o
								hospital não quitou.</p>

							<p>Com colegas despejados de suas casas alugadas, com corte de
								água e luz e pagando altos juros bancários e nos boletos, a
								funcionária Maria Cristina Oliveira Silva – também diretora do
								SINDISAÚDE – fez hoje um apelo à população, em entrevista de
								rádio, para que participem da caminhada e apóie o movimento
								grevista.</p>

							<p>Ela, que trabalha no hospital há 20 anos, diz reconhecer a
								situação enfrentada pelo HCFC, MS apela aos empresários da
								cidade para que ajudem a resolver a crise. A diretora de
								Finanças do Sindicato, Maria Tedesco, explicou na ocasião, que o
								movimento busca, antes de tudo, além de obter a regularização do
								pagamento dos salários e das condições de trabalho, manter o
								hospital com suas portas abertas e prestando um atendimento de
								qualidade aos pacientes.</p>

							<p>Em visita ao acampamento montado defronte ao Frei Clemente, o
								presidente da Federação dos Trabalhadores em Saúde do RS, Milton
								Kempfer, afirmou que a greve não é contra o hospital, mas a
								favor do direito dos funcionários.</p>

							<p>Ele afirmou que é possível salvar o HCFC porque a comunidade
								tem se mostrado solidária, mas cobrou da Federação dos Federação
								das Santas Casas e Hospitais Beneficentes, Religiosos e
								Filantrópicos do RS uma ação contundente contra o Estado, que
								deve R$ 300 milhões em IOSP (Incentivo de Cofinanciamento da
								Assistência Hospitalar), que, com R$ 25 milhões, ajudavam no
								custeio dos hospitais e que o governo Sartori deixou de cumprir.
							</p>


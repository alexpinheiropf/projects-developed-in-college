						<nav id="noticia">
							<p>A presidente do SINDISAÚDE, Terezinha Perissinotto, e os
								diretores, Maria Tedesco, Oziel Ávilla, Cristina Oliveira e
								Rosane de Fátima Ávila Machado estiveram hoje (14/07) em Porto
								Alegre, participando do Seminário sobre Violência Obstétrica.
								Organizado pela Secretaria de Política das Mulheres, pela
								Diretoria de Formação e pelo Comitê de Gênero, Raça, Juventude e
								LGBT da Federação dos Trabalhadores em Saúde (FEESSERS), o
								evento trouxe ao debate o importante tema pouco abordado no
								Brasil, a Violência Obstétrica.</p>

							<p>Ela trata da apropriação do corpo e processos reprodutivos das
								mulheres por profissionais da saúde, por meio de tratamento
								desumanizado, abuso de medicalização e patologização dos
								processos naturais, causando perda da autonomia e capacidade de
								decidir livremente sobre seus corpos, impactando na sexualidade
								e negativamente na qualidade de vida das mulheres.</p>

							<p>Pela manhã, o evento recebeu a palestra da juíza Daniela Valle
								da Rocha (2ª Diretora de Cidadania e Direitos Humanos, membro da
								AJD – Associação de Juízes para a Democracia e que foi advogada
								nas áreas de Direitos Humanos e Trabalhista).</p>

							<p>Na parte da tarde, o tema foi aprofundado, tocando nas causas
								da violência obstétrica na diversidade social de igualdade de
								gêneros. O seu palestrante foi o coordenador do Núcleo
								Psicopedagógico da Santa Casa de Alegrete, o psicólogo e
								pedagogo Rodrigo Dalosto Smolareck.</p>

							<p>Na sequência, foi apresentada uma dinâmica teatral sobre o
								tema com apresentação da peça “SEQUELAS”, tratando de um
								cenário, onde as atrizes Gianna Soccol e Letícia Pagot,
								dirigidas por Diego Ferreira, partem de relatos reais de
								pacientes e trabalha de forma sensível, dando o tom das marcas
								que são deixadas para o resto de suas vidas.</p>

							<p>Projeto de Lei</p>

							<p>A deputada Manuela d Ávila (PCdoB) tem uma proposta de projeto
								de lei tratando da questão da violência obstétrica, inspirada em
								lei já em vigor em Santa Catarina. Antes de ser protocolada, no
								entanto, a matéria será debatida por um grupo de trabalho,
								integrado por profissionais da saúde e por mulheres. O anúncio
								desta medida foi levada ao encontro pela assessora parlamentar
								Cristina Machado. Manuela preside a Procuradoria da Mulher da
								Assembléia Legislativa.</p>


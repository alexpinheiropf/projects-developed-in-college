							<p>É de 3,5% de reajuste a proposta da patronal para os
								trabalhadores das Santas Casas e Hospitais Beneficentes,
								Religiosos e Filantrópicos do RS para este ano. A proposta foi
								feita hoje pela manhã em Porto Alegre em reunião com dirigentes
								da Federação dos Trabalhadores em Saúde (FEESSERS).</p>

							<p>O presidente da FEESSERS foi enfático, afirmando não haver
								condições de se aceitar o percentual oferecido. "Menos que a
								inflação do período não fecharemos acordo", afirmou. Segundo
								Milton Kempfer, a luta pela cobrança dos recursos não repassados
								pelo estado aos hospitais termina onde começa o direito dos
								trabalhadores e as suas perdas salariais.</p>

							<p>Para ele, já é grande o número de adoecimento dos
								funcionários, com contas em atraso e pressão por atendimento.
								"Não podemos pagar sozinhos esta conta," observou, acrescentando
								que "já estamos perdendo vidas em meio à crise instalada pelo
								Governo Sartori", enfatizou. O dirigente entende que o Estado
								deve achar um meio de priorizar a saúde.</p>

							<p>Milton defende a intensificação das mobilizações dos
								trabalhadores da saúde nas suas bases e em torno dos
								representantes públicos, como os deputados estaduais, federais,
								senadores, defensores públicos e o governador. Para ele, não são
								só os dirigentes dos SINDISAÚDES que devem se manifestar
								publicamente. "As comunidades também devem unir-se em torno do
								assunto e firmar pé na busca da garantia do atendimento e de um
								reajuste salarial merecido para os trabalhadores", concluiu.</p>


								<p><b>O público alvo da Campanha CUIDANDO DE QUEM CUIDA são os trabalhadores da saúde dos hospitais da região.</b></p>
								

								<p>A Universidade Federal da Fronteira Sul (UFFS) - Campus Passo Fundo, 
									a Federação dos Trabalhadores em Saúde do RS (FEESSERS) e o SINDISAÚDE 
									de Passo Fundo fazem o lançamento, nesta quinta-feira, dia 06/10, às 14h, 
									da Campanha CUIDANDO DE QUEM CUIDA, voltada para os profissionais 
									que atuam nos hospitais de Passo Fundo e da região.</p>

									<p>A campanha, que tem como slogan <b> “CUIDA MELHOR DO OUTRO, QUEM CUIDA BEM DE SI”</b>,
										integra o Outubro Rosa e o Novembro Azul, estendendo-se pelos dois meses com 
										atividades integradas de prevenção à saúde de mulheres e de homens que trabalham no setor.</p>

										<p>Um dos seus principais pontos será a realização de um questionário para levantamento da 
											situação de saúde dos trabalhadores(as) da saúde, cujo resultado o Curso de Medicina da 
											UFFS transformará em um Projeto de Extensão.</p>

											<p>O movimento, que será estendido a todo o estado por meios dos outros 26 SINDISAÚDES 
												filiados à FEESSERS, conta com o apoio da Secretaria Municipal da Saúde de Passo Fundo, 
												do Conselho Regional de Enfermagem (COREN-RS), do Sindicato dos Enfermeiros do 
												RS (SERGS), do Hospital da Cidade (HC) e do Hospital São Vicente de Paulo (HSVP).</p>

												<p>O público-alvo estimado é de 25 mil trabalhadores da saúde e seus familiares, 
													estudantes, docentes e técnicos das universidades e cursos técnicos que se 
													integrarem às ações em outras cidades do Rio Grande do Sul.</p>

												<p>Dirigentes sindicais e trabalhadores da saúde serão os agentes multiplicadores 
													da campanha, oferecendo um folder explicativo e laços rosa e azul, entrelaçados,
													 para seus colegas, conversando com eles e orientando-os sobre a campanha conjunta.</p>

												<p><b>Dia 06/10 – quinta-feira – 14h</b></p>
												<p><b>Local: SINDISAÚDE de Passo Fundo – Rua Eduardo de Brito, 168.</b></p>


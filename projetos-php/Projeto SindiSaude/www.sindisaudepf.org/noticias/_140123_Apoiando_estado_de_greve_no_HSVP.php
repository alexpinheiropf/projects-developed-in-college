							<p>Em duas assembleias realizadas nesta segunda-feira, 20, cerca
								de 500 técnicos de enfermagem do Hospital São Vicente de Passo
								Fundo decidiram pela paralisação das suas funções. Eles pedem um
								reajuste de 25% sobre o salário e melhores condições de
								trabalho.</p>

							<p>De acordo com a presidente do Sindisaúde, Teresinha
								Perissinoto, os trabalhadores reivindicam um reajuste salarial
								que há muito tempo não é concedido. A classe pretende um aumento
								de 25% sobre o salário de R$ 1.246,00, além de melhores
								condições de trabalho, a contratação de mais funcionários, plano
								de saúde, adicional de insalubridade sobre o salário base e vale
								alimentação. “O sindicato vai estar junto com os trabalhadores,
								vamos nos empenhar ao máximo para que essa greve dê certo.
								Estaremos nessa jornada para melhorar a situação dos técnicos de
								enfermagem”, ressaltou Teresinha.</p>

							<p>Segundo Perissinoto o sindicato já procurarou a patronal para
								abrir negociações sobre a situação. “Nós procuramos a direção do
								hospital para negociar as exigências dos funcionários. A
								patronal mostrou boa vontade em conversar, apesar de ainda não
								terem feito nenhuma proposta. Vamos manter o diálogo”, explicou.</p>

							<p>Na quinta-feira, 23, em outra assembleia, será definido o dia
								e a hora que a greve iniciará e será feito o comunicado ao
								Hospital São Vicente, conforme diz a lei.</p>


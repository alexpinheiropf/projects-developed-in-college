							<p>Crise se estende também a outros hospitais do estado.</p>

							<p>A população apoiou com aplausos a manifestação que reuniu hoje
								(16/10), à tarde, dezenas de funcionários do Hospital de
								Caridade Feri Clemente em passeata pelo Centro de Soledade. Eles
								completam amanhã uma semana de greve.</p>

							<p>Dirigentes do SINDISAÚDE organizaram o manifesto que contou
								com a participação do Sindicato dos Trabalhadores Rurais de
								Soledade e Mormaço, do 28º Núcleo do Cpers, professores do
								Instituto Polivante, Sindicato dos Municipários vereadores e
								integrantes da comunidade.</p>

							<p>Ao longo do percurso, a diretora de Finanças do SINDISAÚDE,
								Maria Tedesco, explicava a motivação do manifesto “de
								conscientizar a comunidade soledadense e regional sobre a
								situação preocupante em que se encontram os cerca de cem
								trabalhadores do Frei Clemente com salários em atraso”.</p>

							<p>Os servidores públicos estaduais, também paralisados,
								aproveitaram para protestar pelo constante parcelamento de seus
								salários por parte do Governo Sartori.</p>

							<p>Durante a caminhada, várias pessoas fizeram uso da palavra,
								tecendo críticas ao Governo do Estado, que agrava a situação do
								Frei Clemente com o atraso nos repasses.</p>

							<p>O contrato do estado com o hospital para atendimento pelo SUS
								prevê o repasse de R$ 492 mil por mês. No entanto, desde o
								inicio do ano, o repasse é feito de forma parcial, e o atraso no
								pagamento é que tem causado a crise do HCFC e transformado a
								vida de seus funcionários em um caos.</p>

							<p>Crise também em outros hospitais do estado</p>

							<p>Em Cruz Alta, os funcionários do Hospital Brasilina Terra
								também decidiram paralisar as suas atividades em função da falta
								de 55% do salário de agosto e 100% de setembro.</p>

							<p>O presidente da Federação dos Trabalhadores da Saúde do RS,
								Milton Kempfer, alertava hoje, no Encontro Estadual de
								Dirigentes e Trabalhadores em Saúde, em Porto Alegre, que a
								situação deve se estender a outros hospitais filantrópicos por
								conta da inadimplência do Estado.</p>

							<p>Ele citou a Beneficência Portuguesa de Porto Alegre, Fundação
								Sapucaia do Sul, os hospitais Centenário de São Leopoldo, de
								Caridade de Ijui, de Caridade de Canguçu, Abosco de Santa Rosa,
								e as Santas Casas de Jaguarão, Rio Grande, Pelotas, Livramento e
								de Bagé.</p>


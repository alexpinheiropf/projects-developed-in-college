							<p>O Sindicato dos Empregados em Estabelecimentos em Serviços de
								Saúde de Passo Fundo e Região – SindiSaúde está chegando neste
								ano de 2013 aos 29 anos de lutas e conquistas para os
								trabalhadores e trabalhadoras da Saúde da nossa região. Essa
								idade está sendo alcançada com as marcas da garra, lutas e muito
								trabalho por uma vida melhor, condições de trabalho dignas e
								para que direitos trabalhistas sejam respeitados.</p>

							<p>A fundação do nosso Sindicato, em 14 de maio de 1984, tinha o
								objetivo de melhorar as condições de vida dos trabalhadores (as)
								da Saúde de Passo Fundo. Logo após, em setembro de 1984, houve a
								eleição da primeira direção do SindiSaúde. Foi eleito como
								presidente o companheiro Jorge Luiz Ponzoni (já falecido). A
								partir do seu funcionamento, os resultados começaram a ocorrer
								em 1985 foi fechado o primeiro Acordo Coletivo, no qual ficou
								garantido aos trabalhadores (as) o Piso e o Reajuste Salarial
								acima da inflação, os uniformes, quinquênios e outras conquistas</p>

							<p>Em 1986, com a criação do Plano Cruzado, o Sindicato Patronal
								não se dispôs a negociar. Na época era reivindicado, além do
								reajuste do salário, a jornada de 36 horas. Isto motivou uma
								greve que durou 42 dias, na qual os trabalhadores (as) das
								principais empresas de Saúde de Passo Fundo paralisaram suas
								atividades. Na oportunidade, foi conquistado um reajuste de
								172%. Com esta luta, os hospitais cederam e houve a redução da
								jornada, além de melhorias econômicas, sociais e o fechamento de
								vários Acordos Coletivos.</p>

							<p>A partir disso, dez municípios da região aderiram ao
								SindiSaúde. Houve greves em Tapejara, Carazinho, Palmeira das
								Missões, Getúlio Vargas e Soledade por melhorias nos salários e
								nas condições de trabalho. Palmeira das Missões também foi palco
								de grandes mobilizações, culminando em avanços econômicos no
								salário, redução da jornada de trabalho para seis horas diárias
								e conquista de uniformes, entre outros.</p>

							<p>Após muitas lutas, em 1989 o Sindicato estendeu sua base para
								mais 30 municípios. Com a união de todos, houve mais conquistas:
								a qualificação dos trabalhadores através de cursos, adicional
								noturno de 45%, repouso e refeições.</p>

							<p>Essa é apenas uma pequena amostra da história vitoriosa do
								SindiSaúde. Os anos 90 e até 2012 foram marcados por muito
								trabalho e lutas por direitos que a Constituição Federal garante
								a cada cidadão e por novas conquistas para os trabalhadores (as)
								da Saúde. Sabemos que ainda há muita luta pela frente rumo aos
								nossos muitos objetivos. Chegando aos 30 anos, o SindiSaúde
								valendo-se do amadurecimento obtido ao longo do tempo e das
								batalhas, tem consciência de que é necessário alimentar ainda
								mais a busca pela valorização do trabalho e do trabalhador (a)
								da Saúde com a ampliação dos direitos.</p>

							<p>Enfim, o nosso Sindicato se confunde, não apenas com a
								trajetória dos trabalhadores (as) da Saúde, mas com a trajetória
								de todos os trabalhadores (as) brasileiros (as). Isto porque
								sempre priorizou os seus fundamentos nos direitos
								constitucionais do cidadão, principalmente no que diz respeito
								aos direitos trabalhistas. A consequência, como se pode
								observar, é a solidez e o respeito que o SindiSaúde tem perante
								todas as esferas da nossa sociedade. Ou seja, nosso Sindicato
								sempre teve posição. E a sua posição sempre foi ao lado dos
								trabalhadores (as) da Saúde, defendendo-os nos seus interesses
								individuais e coletivos.</p>

							<p>
								<strong>Avançar, consolidar e vender! Este é nosso lema. Venha
									junto com o Sindicato fazer uma história de novas conquistas e
									realizações.</strong>
							</p>


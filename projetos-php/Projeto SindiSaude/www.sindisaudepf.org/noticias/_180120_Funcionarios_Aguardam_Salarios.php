						<nav id="noticia">
							<p>Diariamente pressionados pela falta de dinheiro, apesar de
								trabalharem diariamente no Hospital de Caridade Palmeira das
								Missões (HCPM), os funcionários da entidade – com salários
								atrasados -, temem pelo futuro, uma vez que já estão correndo
								risco de perder o crédito no mercado local. Preocupados com a
								situação grave do hospital, os dirigentes do SINDISAÚDE de Passo
								Fundo e da Federação dos Trabalhadores em Saúde do RS – FEESSERS
								participaram de uma caminhada até à Prefeitura da cidade para
								falarem com o prefeito Eduardo Freire e pedir apoio por meio do
								pagamento dos repasses em atraso do governo SARTORI. Como o
								prefeito está de férias, uma comissão foi recebida por
								secretários municipais. No dia 23, caso o pagamento não ocorra,
								os funcionários vão acampar em frente à Prefeitura anunciou
								Terezinha Perissinotto, presidente do Sindicato.</p>

							<p>O grupo ouviu dos integrantes do governo que os salários
								atrasados de dezembro, deverão ser pagos no dia 23, após a
								entrada de valores devidos pelo governo SARTORI. Segundo o
								secretário da saúde Paulo Fernandes a dívida em atraso com o
								estado chega a R$ 2 milhões e 700 mil, correspondente ao
								pagamento de serviços. Ainda de acordo com o secretário, na
								próxima semana deve entrar o valor do MAC (média e alta
								complexidade) o que deverá ser utilizado para pagamento da
								folha. E, pressionado sobre os investimentos da prefeitura no
								hospital, prosseguiu sua fala dizendo que a “prefeitura repassa
								R$ 180 mil para manter a instituição aberta, sendo distribuído
								entre a folha, médicos e fornecedores”.</p>

							<p>DIREITO MÍNIMO</p>

							<p>Terezinha Perissinotto afirmou aos secretários, que a presença
								da comissão integrada por dirigentes do SINDISAÚDE, da Federação
								e das funcionárias do HCPM, Iliane Oselame e Fátima Martins,
								estava destinada a garantir o pagamento da folha, mas também de
								solicitar o aumento dos valores de repasse para a sustentação do
								mesmo. “Porque os trabalhadores estão exaustos deste descaso dos
								governos, que leva a mensalmente ter de recorrer de pressão para
								conseguir o direito mínimo, que é o pagamento em dia dos
								salários. Além disso, é preciso ter um planejamento transparente
								de gestão, para que possamos saber o que está sendo feito para
								estancar esta sangria diária para ambos os lados”, complementou
								Milton Kempfer.</p>

							<p>Silvestre Becker falou sobre as expectativas frustradas, com a
								arrecadação dos impostos como IPTU, IPVA e outros, que, em
								geral, desafogam o caixa nos meses de janeiro e fevereiro. Maria
								Tedesco, diretora de Finanças de Sindicato, observou que a baixa
								nos pagamentos se deve, entre outros fatores, ao não pagamento
								dos salários, “trata-se de uma bola de neve”, acrescentou.</p>

							<p>DESGASTE</p>

							<p>O administrador do Hospital de Caridade Márcio Rafael Slaviero
								diz ser solidário com os trabalhadores e, que vem insistindo em
								buscar soluções para evitar o desgaste mensal a que todos estão
								expostos. Acompanhado de Cássio Roese, diretor administrativo da
								entidade, Márcio informou que o governo federal já depositou 70%
								do MAC, mas que o repasse ainda chegou ao município.</p>

							<p>PEC55</p>

							<p>Estressado com a situação, o procurador do Município Jorge
								Adones dos Anjos referiu à decisão “nobre” da prefeitura em ter
								judicializado as dívidas com o estado e exigido o pagamento em
								dia, o que segundo ele, pelo menos tem garantido os pagamentos
								na lista dos prioritários. Os secretários disseram ter
								consciência do desastre e caos na saúde, tanto no governo
								federal quanto estadual. E, que a situação deve piorar,
								acrescentou o presidente da FEESSERS, Milton Kempfer, porque a
								PEC 55 - que congela os investimentos em saúde por 20 anos –
								“vai trazer consequências inimagináveis no momento”.</p>

							<p>REJEIÇÃO AO HOSPITAL REGIONAL</p>

							<p>Milton Kempfer disse ao secretário da fazenda Silvestre Becker
								“que é triste ver a montanha de dinheiro que entra nos cofres da
								prefeitura, voltados para projetos que não tem como realizar,
								enquanto os trabalhadores que sustentam o sistema e o hospital,
								que lida com vidas, ficam à margem e até passam fome”.
								Aproveitou o presidente para dizer da contrariedade dos
								sindicatos e da federação com a construção do Hospital Regional,
								e que, em sua opinião, os recursos deveriam ser canalizados para
								melhorar o que já existe e aumentar a sua produtividade,
								garantindo funcionários e sociedade na estabilidade de um
								atendimento qualificado. Como o prefeito, Silvestre também é
								favorável à implantação do novo hospital.</p>

							<p>Para encerrar a discussão, foi sugerido que a prefeitura
								avalie a possibilidade de passar a ter gestão PLENA no hospital,
								o que facilitaria o gerenciamento, porque o pagamento seria
								feito diretamente entre governo federal e município. O
								secretário de saúde disse que estão estudando a proposta há um
								ano.</p>

							<p>REUNIÃO NA PRAÇA</p>

							<p>Após o encontro, os diretores da federação e do sindicato
								chamaram uma conversa na praça em frente ao hospital e
								informaram o resultado da reunião na prefeitura. Entre
								indignados e chocados com a situação, os trabalhadores decidiram
								parar o trabalho no dia 23, caso os salários não sejam
								depositados. “A nossa briga não é contra o prefeito, nossa luta
								é pelo direito dos trabalhadores de serem pagos em dia para
								viverem com dignidade”, concluiu Terezinha Perissinotto.</p>


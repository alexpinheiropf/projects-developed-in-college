							<p>A diretora do Sindicato Maria Tedesco participou ontem à tarde
								em Porto Alegre da primeira reunião da Comissão de Auxiliares e
								Técnicos de Enfermagem do COREN-RS.</p>

							<p>A Comissão, criada em dezembro de 2015, tem a atribuição de
								ser um fórum de debate com os profissionais das diferentes
								regiões do estado, em especial dos profissionais auxiliares e
								técnicos de enfermagem com o objetivo de promover o debate sobre
								irregularidades da profissão, traçando estratégias de
								regularização, promovendo, assim, um cuidado de enfermagem
								isento de riscos aos profissionais e usuários no Estado,
								conforme a Decisão Nº 122/2015.</p>

							<p>A Comissão é composta por 08 (oito) auxiliares e/ou técnicos
								de enfermagem representantes das diversas regiões do estado e
								até três profissionais representantes da Gestão do COREN-RS,
								além do Presidente.</p>

							<p>Sob a coordenação do presidente do COREN-RS, Daniel Menezes de
								Souza, o primeiro encontro foi de apresentação e entrosamento.
								No entanto, Maria Tedesco, o diretor da FEESSERS Carlos Weber, a
								presidente do SINDISAÚDE de Pelotas Bianca D’ Carla e a diretora
								do Sindiesca, Elizandra Dill, enfatizaram no encontro que a
								comissão não terá um sentido apenas figurativo, mas de
								efetividade num trabalho conjunto em benefício de auxiliares e
								técnicos e de troca de informações para resolver as suas
								demandas regionais. A próxima reunião da Comissão será no mês de
								agosto. Rosa Pitsch (MTb-5015).</p>


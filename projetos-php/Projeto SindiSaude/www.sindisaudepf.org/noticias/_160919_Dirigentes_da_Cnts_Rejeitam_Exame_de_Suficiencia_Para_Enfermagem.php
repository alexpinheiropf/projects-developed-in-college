							<p>Na quarta-feira (17/08), a presidente do SINDISAÚDE e diretora
								da CNTS, Terezinha Perissinotto, participou em Brasília do
								seminário sobre exame de suficiência na graduação em enfermagem.
								O evento, organizado pela CNTS, foi marcado por cobranças na
								qualidade do ensino e definiu a posição contrária dos dirigentes
								da entidade em relação ao Projeto de Lei 4.930/2016, proposto
								pelo Cofen - e apresentado pelo deputado Lúcio Viera Lima
								(PMDB-BA) -, que prevê a aprovação de técnicos e enfermeiros
								graduandos no exame como requisito obrigatório, nos mesmos
								moldes da OAB, para exercer a profissão.</p>

							<p>A enfermeira Kênia Lara da Silva, da Associação Brasileira de
								Enfermagem e membro da Comissão Permanente de Educação, relatou
								que a situação de desigualdade entre os que são aprovados e os
								que não são no exame de suficiência é bem grande. “Os aprovados
								são os que fazem cursos e têm mais tempo para estudar e a
								maioria são homens. Já os não aprovados são os de baixa renda,
								que trabalham em mais de um emprego e são mulheres. Segundo a
								professora, o que tem que ser feito é fiscalizar as faculdades
								que oferecem os cursos com má qualidade e a proposta precisa ser
								arquivada.</p>

							<p>A professora relatou ainda que a experiência em outros países
								como os Estados Unidos não foi tão bem aceita. “As competências
								que são realizadas com os profissionais que realizam o exame,
								não são as melhores esperadas para o desempenho da profissão
								desejada, no contexto melhor em saúde”, disse.</p>

							<p>Outro ponto apresentado pela enfermeira que, segundo ela,
								resolveria o problema da falta de qualidade na atuação dos
								enfermeiros, auxiliares e técnicos de enfermagem seria a
								ampliação de postos de trabalho. “Talvez a nossa discussão seja
								ampliar postos de trabalho para que a gente equilibre mais essa
								relação entre paciente e enfermeiro em geral. Se a gente for
								acompanhar a ampliação tem sido muito pequena diante do que é
								preciso para qualificar o processo da enfermagem. Imagine uma
								enfermeira cuidando de uma enfermaria com 40 leitos, no mínimo
								teria que ter umas cinco enfermeiras nesse lugar, mas é a
								realidade, essa teria que ser a nossa discussão”.</p>

							<p>Além da Confederação, a Associação Brasileira de Enfermagem –
								ABEn, Associação Nacional dos Auxiliares e Técnicos de
								Enfermagem – ANATEN, Confederação Nacional dos Trabalhadores em
								Seguridade Social – CNTSS, Executiva Nacional dos Estudantes de
								Enfermagem - ENEEnf e Federação Nacional dos Enfermeiros – FNE
								se posicionaram unânimes contra o projeto.</p>


							<p>No local foram esclarecidos sobre a situação do atraso de
								salários a que estão constantemente submetidos, sendo informados
								de que na noite de ontem – cerca de 22h – foi feito o depósito
								de 60% do salário relativo ao mês de outubro.</p>

							<p>Em contato com a administração do hospital, a diretora de
								Finanças do Sindicato, Maria Tedesco, foi informada de que,
								dificilmente sejam depositados os 40% restantes antes do dia 05
								de dezembro. “Eles alegam que o Governo do Estado deve em torno
								de R$ 1,5 milhão e não tem repassado recursos”, observa a
								dirigente.</p>

							<p>Mesmo com esta providencia tomada pela administração do
								hospital, o SINDISAÚDE vai manter a realização da Assembleia
								Geral Extraordinária que irá se realizar na sexta-feira, dia 02
								de dezembro, às 12h15min, na sede do Sindicato dos Municipários
								(SINPAM) - Rua Marechal Floriano, nº 1026 - , a fim de
								deliberarem sobre a seguinte ordem do dia:</p>

							<p>1) Discutir a atual situação dos trabalhadores da Associação
								do Hospital de Caridade de Palmeira das Missões – RS;</p>

							<p>2) Deliberar sobre a paralisação coletiva da prestação de
								serviços. A greve será discutida e todas as suas resoluções
								serão aprovadas conforme Lei nº 7783/89.</p>

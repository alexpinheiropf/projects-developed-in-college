								Conforme a presidente do SINDISAÚDE, Terezinha Perissinotto, o
								Governo do Estado efetuou parte dos recursos devidos ao HC, o
								que permitiu efetuar o pagamento da folha de junho. Com isto,
								segue ainda em aberto o mês de julho e, sem ela, o pessoal não
								retorna ao trabalho, garante a dirigente.</p>

							<p>No acampamento montado na praça Getúlio Vargas estão sendo
								servidos café da manhã, almoço e janta para os funcionários e
								familiares, contando com doações de populares diretamente ou que
								angariam os alimentos no comércio local. “O carinho da população
								com a nossa causa é imensa”, declara Terezinha, agradecida.</p>

							<p>Em outra assembleia, realizada no início da manhã, os
								enfermeiros do HC também decidiram paralisar suas atividades,
								aderindo oficialmente ao movimento grevista dos trabalhadores da

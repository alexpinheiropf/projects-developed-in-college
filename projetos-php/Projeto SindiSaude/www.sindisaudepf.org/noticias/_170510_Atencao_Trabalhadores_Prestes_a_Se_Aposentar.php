							<p>O assessor jurídico do SINDISAÚDE de Passo Fundo e Região,
								Éverton de Ré, orienta aos trabalhadores da saúde expostos a
								condições insalubres, que estiverem prestes a entrar com pedido
								de aposentadoria, e que que quiserem continuar a trabalhar no
								mesmo setor, que ingressem judicialmente solicitando uma
								permissão especial a fim de resguardar seu direito e evitar o
								eventual cancelamento da aposentadoria. Para maiores
								esclarecimentos, é necessário entrar em contato com o seu
								sindicato.</p>

							<p>De acordo com ele, “uma dúvida constante dos trabalhadores que
								laboram em locais insalubres refere-se à necessidade de
								afastamento do trabalho em caso de aposentadoria especial”. Ele
								explica que, em que pese o INSS exigir, sob pena de cancelamento
								do benefício, que após a concessão da aposentadoria especial o
								trabalhador se afaste daquele local de trabalho ou atividade que
								o expõe a condições nocivas para sua saúde. “Tal exigência
								conflita com o direito constitucional do livre exercício de
								qualquer trabalho, ofício ou profissão”, explica.</p>

							<p>O Supremo Tribunal Federal (STF), através do Recurso
								Extraordinário (RE) 788092, reconheceu a repercussão geral do
								assunto, e analisará a constitucionalidade do § 8º do art. 57 da
								Lei nº 8.213/1991, que prevê o cancelamento automático da
								aposentadoria especial do beneficiário que retorna às atividades
								de trabalho nocivas à saúde.</p>

							<p>Enquanto o STF não decide de maneira definitiva, ele orienta
								aos trabalhadores que tiverem interesse, que pleiteiem
								judicialmente a permissão para continuarem laborando no seu
								local habitual, mesmo após a aposentadoria especial, pedido este
								que pode ser feito de forma concomitante ao pedido de
								aposentadoria ou em momento posterior, a fim de resguardar seu
								direito e evitar o eventual cancelamento da aposentadoria pelo
								INSS.</p>


							<p>Emocionada, uma senhora passa em frente ao hospital São
								Vicente de Passo Fundo (HSVP). Ao parar para conversar com os
								técnicos em enfermagem que estão em greve desde ontem, 27, ela
								conta a razão da sua emoção: técnica de enfermagem aposentada,
								ela esteve presente na primeira greve ocorrida naquele hospital,
								em março de 1986.</p>

							<p>Revivendo o passado, como ela mesma diz, Eli Lopes da Silva
								fez questão de apoiar os cerca de 400 trabalhadores que aderiram
								ao movimento e estão lutando por melhores salários e valorização
								profissional. “Por um lado é triste, porque isso mostra que as
								pessoas não reconhecem o valor do nosso trabalho. Mas, por
								outro, o importante é a mobilização da categoria, a situação
								permanece a mesma, de desrespeito com os profissionais”,
								ressaltou Eli.</p>

							<p>Da Silva, participou da primeira grande greve que o HSVP
								presenciou. Foi em 1986, do dia 19 de março a 30 de abril, 42
								dias de paralisação. Segunda ela, os técnicos de enfermagem têm
								uma jornada de trabalho reduzida nos dias de hoje, graças àquela
								greve. “Fiquei emocionada quando vi a mobilização. Acho que as
								pessoas têm que lutar pelos seus direitos. A nossa greve foi
								bastante sofrida, pois foi a primeira. Mas, conseguimos o apoio
								de todos no hospital, inclusive dos médicos”, relembrou.</p>

							<p>Ao se encontrar com a sua amiga Teresinha Perissinoto,
								presidente do Sindisaúde de Passo Fundo, fez questão de elogiar
								a organização e de ressaltar o espírito guerreiro desses
								trabalhadores. “Uma coisa é a vida dessas pessoas antes da
								greve, outra será depois que a greve encerrar, eles serão muito
								mais respeitados”, disse Teresinha.</p>

							<p>Com 24 anos de trabalhos prestados ao hospital São Vicente e à
								população de Passo Fundo, Eli Lopes da Silva espera que a
								história se repita, e que, assim como há 28 anos, os técnicos de
								enfermagem e trabalhadores dos HSVP saiam vitoriosos dessa luta.</p>


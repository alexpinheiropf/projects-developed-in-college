							<p>Na manhã do dia 25 de setembro, Milton Kempfer, presidente da
								Feessers, se reuniu com o dirigente da Fehosul, Claudio Algayer
								– e demais representantes da entidade – a fim de debater a
								Convenção Coletiva de Saúde Estadual.</p>

							<p>De acordo com o Kempfer, alguns sindicatos do interior
								conseguiram fechar acordos individuais, no entanto, o objetivo é
								que seja assinada uma pauta unificada, que beneficie a todos os
								trabalhadores da saúde do Estado. “Queremos garantir e ampliar
								as cláusulas sociais e conquistar o reajuste salarial que
								contemple o índice do INPC mais um aumento real de 10%”, afirma.</p>

							<p>Também participaram da reunião os presidentes do Sindisaúdes
								de Lajeado, São Borja e Passo Fundo, respectivamente, Roberto
								Souza, Neuza Rambo e Terezinha Perissinoto, além da
								representante do Sindicato de Alegrete, Claudia Martins da Cruz.</p>

							<p>A próxima reunião entre as entidades será realizada no dia
								10.10, na sede da Fehosul. E, para organizar e agrupar as
								informações para o andamento da negociação, Kempfer convida um
								representante de cada Sindisaúde filiado para que compareça em
								uma reunião na FEESSERS, no dia 09.10, às 14h.</p>

							<p>“Será um reunião muito importante e os diretores devem trazer
								todas as informações possíveis sobre a sua região, como por
								exemplo, a última convenção coletiva com a Fehosul, ou Sindicato
								Patronal, e a cópia dos acordos coletivos individuais com
								empresas de saúde ligadas à Federação dos Hospitais”, explica.</p>


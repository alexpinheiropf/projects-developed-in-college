								menor tempo possível, retomando e normalizando o atendimento à
								população.</p>

							<p>Enquanto o impasse não tiver solução, até que se resolva o
								conflito, solicitamos aos gestores da saúde de Passo Fundo e dos
								demais Municípios da região que procurem encaminhar seus
								pacientes para outros centros de atendimento.</p>


							<p>O SINDISAÚDE está acompanhando a ação por meio de sua diretora
								de Finanças, Maria Tedesco.</p>

							<p>A força-tarefa, que busca adequar saúde e segurança no
								trabalho em hospitais no Rio Grande do Sul e já esteve nos
								hospitais Mãe de Deus e Conceição (em Porto Alegre), Unimed
								Caxias do Sul, Tacchini (em Bento Gonçalves) e Dom João Becker
								(Gravataí)</p>

							<p>Conceição (em Porto Alegre), Unimed Caxias do Sul, Tacchini
								(em Bento Gonçalves) e Dom João Becker (Gravataí). Começou, às
								8h da terça-feira (04/03), na Associação Hospitalar Beneficente
								Hospital São Vicente de Paulo (HSVP), em Passo Fundo, a sexta
								operação da força-tarefa de adequação das condições de saúde e
								segurança no trabalho em hospitais no Rio Grande do Sul. A
								unidade está localizada na rua Teixeira Soares, 808, Centro, e
								possui 3.200 trabalhadores. O grupamento operativo é coordenado
								pelo Ministério Público do Trabalho (MPT). O objetivo é
								investigar condições de saúde e de segurança dos trabalhadores,
								em todos os postos de trabalho, à semelhança do que é feito nos
								frigoríficos, desde janeiro de 2014. Os principais problemas
								enfrentados no setor são doenças de coluna pelo esforço de
								movimentar pacientes, acidentes com perfurocortantes e
								contaminação biológica.</p>

							<p>O grupo foi recebido pelo superintendente-executivo, Ilário
								Jandir De David, que designou o gerente de Recursos Humanos,
								Jairo Davoglio, como interlocutor pelo hospital. Foram
								solicitados 75 documentos ao nosocômio. Os integrantes da
								operação se dividiram em quatro equipes para otimizar a
								fiscalização: ergonomia, saúde do trabalhador e da trabalhadora
								/ dimensionamento de pessoal, segurança e habilitação /
								responsabilidade profissional. A inspeção deverá se estender até
								a próxima sexta-feira (7/4), quando a empresa será notificada do
								resultado. No final da tarde dessa segunda-feira (3/4), os
								integrantes da força-tarefa reuniram-se para ultimar os
								preparativos da operação.</p>

							<p>Parceiros</p>

							<p>A operação tem apoio técnico de 4 Coordenadorias Regionais de
								Saúde (CRSs): 5ª, 7ª, 8ª e 11ª, vinculadas à Secretaria Estadual
								da Saúde (SES); de 5 Centros Regionais de Referência em Saúde do
								Trabalhador (Cerests): Erechim, Ijuí, Palmeira das Missões,
								Passo Fundo e Santa Cruz do Sul, vinculados aos seus
								municípios-sede, além do Conselho Regional de Engenharia e
								Agronomia do Estado do Rio Grande do Sul (CREA-RS).</p>

							<p>A ação tem participação de 34 integrantes. Pelo MPT, os
								procuradores do Trabalho Ricardo Garcia (lotado em Caxias do
								Sul), representante regional da Coordenadoria Nacional de Defesa
								do Meio Ambiente do Trabalho (Codemat), e Priscila Dibi Schvarcz
								(Passo Fundo), responsável pelo inquérito civil (IC), mais o
								chefe da Assessoria de Comunicação do MPT-RS, jornalista Flávio
								Wornicov Portela (Porto Alegre).</p>


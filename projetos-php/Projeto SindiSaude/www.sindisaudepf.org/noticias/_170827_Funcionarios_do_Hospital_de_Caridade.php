						<nav id="noticia">
							<p>Ontem (25/08) à tarde, os funcionários do Hospital de Caridade
								de Palmeira das Missões voltaram ao trabalho, depois de 11 dias
								de paralisação, após receberem a confirmação de que o pagamento
								dos 20% restantes do salário de julho foram depositados em sua
								conta bancária.</p>

							<p>A greve, decidida dia 14 em assembleia foi decorrência do
								atraso de dois meses no pagamento de seus salários. A partir de
								quando o SINDISAÚDE instalou uma barraca na Praça Getúlio
								Vargas, garantindo o revezamento de funcionários no HC para
								atenderem os casos de urgência e emergência no hospital.</p>

							<p>Fundamental para a manutenção do movimento foi o apoio da
								população, que ajudou os trabalhadores e suas famílias com o
								fornecimento de cestas básicas e alimentos não perecíveis,
								segundo a presidente do Sindicato, Terezinha Perissinotto.</p>

							<p>Neste período, os trabalhadores realizaram três passeatas na
								área central da cidade para alertar a população sobre a crise
								pela qual passavam, em decorrência do atraso de dois meses em
								seus salários.</p>

							<p>Também por estes dias, a direção do SINDISAÚDE realizou
								diversas reuniões de negociação com a superintendência do HC.
								Fez vários contatos telefônicos com autoridades e conseguiu
								trazer o deputado estadual Altemir Tortelli (PT) até à cidade.</p>

							<p>Ciente da gravidade da situação, ele conseguiu com o
								secretário adjunto da Saúde, Francisco Paz, que o Estado pagasse
								parte dos recursos devidos ao Hospital da Caridade, o que
								permitiu que os salários em atraso fossem pagos.</p>

							<p>O movimento teve o apoio também da direção da Federação dos
								Trabalhadores da Saúde (FEESSERS) e dos SINDISAÚDES de Cruz
								Alta, Ijuí e Santa Rosa, que vieram a Palmeira das Missões para
								a última manifestação no dia 24.</p>

							<p>Também estiveram ao lado dos trabalhadores do HC, a CUT
								Planalto, o Sindicato Trabalhadores Industria da Alimentação e o
								Sindimáquinas de Passo Fundo, o Movimento dos Trabalhadores
								Desempregados (MTD) e diversos movimentos sociais, além do
								deputado Juliano Roso (PC do B), bem como Neri Gomes, assessor
								do deputado federal Marco Maia (PT).</p>


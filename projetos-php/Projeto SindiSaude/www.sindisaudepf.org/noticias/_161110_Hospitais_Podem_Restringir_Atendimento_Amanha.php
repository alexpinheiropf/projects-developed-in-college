							<p>Movimento contra a PEC 241 visa em especial defender a
								manutenção dos investimentos no SUS.</p>

							<p>O SINDISAÚDE avisa à população de que amanhã (11/11), Dia de
								Mobilização Nacional, os hospitais de Passo Fundo e Região
								poderão paralisar as suas atividades, mantendo apenas os
								serviços de urgência e emergência.</p>

							<p>Neste sentido, convoca a todos os trabalhadores e
								trabalhadoras da saúde, de hospitais, clínicas, laboratórios e
								consultórios para que participem, paralisando suas atividades ou
								participando das mobilizações.</p>

							<p>A decisão está respaldada em reunião do dia 21 de outubro, em
								que a direção da Federação dos Empregados em Estabelecimentos de
								Serviços de Saúde do Estado do RS (FEESSERS) decidiu engajar-se
								na luta contra a Proposta de Emenda Constitucional (PEC) 241 e
								demais medidas restritivas de investimentos e direitos que estão
								tramitando no Congresso, em especial o congelamento de custos na
								área da saúde por 20 anos. A Federação das Santas Casas e
								Hospitais Filantrópicos do RS foi oficiada do fato, por meio de
								correspondência no dia 24 de outubro.</p>

							<p>De acordo com Terezinha Perissinotto, presidente do Sindicato,
								a greve é uma forma legítima que os trabalhadores têm para
								pressionar o governo e os parlamentares de fazer com que
								entendam que o desenvolvimento com geração de emprego e renda,
								não depende do corte nas finanças sociais.</p>

							<p>Ela ainda lança o alerta de que a aplicação da PEC vai agravar
								ainda mais a situação da saúde no estado, que desde 2015, vem
								sofrendo cortes de repasses aos hospitais filantrópicos por
								parte do governo estadual. “Isto já vêm causando a
								dessasistência aos mais necessitados, a redução e fechamento de
								serviços, atrasos nos salários dos funcionários, demora e falta
								de atendimento aos usuários do SUS, agravo de doenças e mortes,”
								observa a dirigente.</p>

							<p>No inicio da tarde de hoje, representantes do movimento
								sindical e dos movimentos sociais da região reuniram-se na sede
								do SINDISAÚDE para articularem as manifestações do dia de
								amanhã.</p>


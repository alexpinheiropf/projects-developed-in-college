							<p>Representantes da Feessers e Sindisaúdes filiados realizaram
								mais um movimento de reivindicação para a criação de uma faixa
								dentro do Piso Mínimo Regional para os auxiliares e técnicos de
								enfermagem, desta vez, em reunião no Conselho de Desenvolvimento
								Econômico e Social do RS (CDESRS), na manhã do dia 16 de outubro</p>

							<p>Na ocasião, Milton Kempfer, presidente da Federação dos
								Trabalhadores, reafirmou a situação da escassez de mão de obra
								qualificada no setor da saúde devido à baixa remuneração</p>

							<p>“Estamos em um momento preocupante, pois há um esvaziamento no
								quadro profissional. Recentemente, um hospital de Curitiba
								fechou suas portas em função da falta de funcionários e aqui no
								RS estamos caminhando para a mesma situação. Precisamos rever
								com urgência o piso salarial e nosso pedido é que inicialmente a
								faixa se assemelhe ao do Estado do Rio de Janeiro, que está
								estipulado em aproximadamente R$ 1 mil”, afirmou.</p>

							<p>Gilmar França, presidente do Sindisaúde-RS, salientou que a
								luta pela implantação da faixa salarial já dura sete anos e que
								a situação de caos engloba a todo o Estado. “Diversas
								instituições, inclusive o Hospital de Clínicas de Porto Alegre
								(HCPA) e o Grupo Hospitalar Conceição (GHC), que possuem
								salários acima da média, estão com problemas para manter
								trabalhadores qualificados. Muitos estão migrando para outras
								áreas, assim como no município de Rio Grande, onde os
								profissionais da saúde estão se transferindo para o polo naval”,
								disse.</p>

							<p>De acordo com Marcelo Danéris, secretário-executivo do CDESRS,
								nos próximos dias o governador Tarso Genro receberá os
								trabalhadores em audiência no Palácio Piratini. Além disso,
								orientou que a valorização dos salários dos trabalhadores seja
								incluída na pauta da Câmara Temática de Saúde do RS e desta
								forma, seja debatida para entrar no Pacto Gaúcho pela Saúde.</p>

							<p>Também estiveram presentes representantes dos Sindisaúdes de
								Passo Fundo, Erechim, Santiago, Lajeado, Caxias do Sul, Rio
								Grande, Cruz Alta, São Gabriel, Ijuí e Santa Rosa.</p>


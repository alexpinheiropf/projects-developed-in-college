						<nav id="noticia">
							<p>HSVP notificado para adequar 190 situações encontradas em 17
								conjuntos de setores e atividades; operação desta semana foi a
								sexta da força-tarefa de adequação das condições de saúde e
								segurança no trabalho em hospitais gaúchos; audiência designada
								para 23 de maio oportunizará à empresa celebração de TAC.</p>

							<p>O Ministério Público do Trabalho (MPT) notificou, na manhã
								desta sexta-feira (7/4), a Associação Hospitalar Beneficente
								Hospital São Vicente de Paulo (HSVP), em Passo Fundo, sobre 190
								irregularidades encontradas em 17 conjuntos de setores e
								atividades. A notificação de 20 páginas é resultado de operação
								realizada, desde terça-feira (4/4), nas dependências do
								nosocômio. Essa foi a sexta ação da força-tarefa de adequação
								das condições de saúde e segurança no trabalho em hospitais no
								Rio Grande do Sul. O Hospital deve adotar providências
								relacionadas na notificação, sem prejuízo da construção de
								cronograma de adequações a ser discutido em audiência
								administrativa designada para 23 de maio, 13h30min. O objetivo
								da reunião será oportunizar ao HSVP celebração de termo de
								ajuste de conduta (TAC), evitando imediata ação civil pública
								(ACP).</p>


							<p>Foram encontrados problemas em diversos setores do Hospital,
								tais como: lavanderia, hospital, central de materiais e
								esterilização (CME), cozinha e padaria, hemodinâmica,
								emergência, marcenaria, arquivo, UTIs e internações, geral
								(todos setores) programas e medidas de proteção de cunho
								prevencionista, equipamenos de proteção individual (EPIs),
								capacitações e treinamentos, melhorias ergonômicas, Serviço
								Especializado em Engenharia de Segurança e em Medicina do
								Trabalho (SESMT), Comissão Interna de Prevenção de Acidente
								(CIPA) e investigações de acidentes de trabalho.</p>

							<p>Especificamente em relação à saúde dos trabalhadores, foram
								identificadas falhas na gestão de saúde e segurança dos
								trabalhadores do Hospital, evidenciadas pela inadequação de
								medidas de proteção coletiva e individual, pela deficiente
								investigação e análise de acidentes de trabalho, pela
								subnotificação de doenças relacionadas ao trabalho, pela
								inadequação nos programas prevencionistas apresentados, bem como
								em outras documentações. Além disso, verificou-se déficit nas
								capacitações e treinamentos dos trabalhadores, além de
								sobrecarga de trabalho.</p>

							<p>Também foram encontradas irregularidades, como a autoclave da
								Central de Materiais e Esterilização (CME) sem as proteções
								laterias de segurança, risco de queda e risco ergonômico (no
								arquivo geral), movimentação de carga com peso acima do
								recomendado (na cozinha), postos de trabalho inadequados
								ergonomicamente, trabalho em altura com risco de queda, guincho
								para elevação de pacientes obstruindo mangueira de incêndio,
								descarte de materiais contaminados sem luvas e demais
								equipamentos de proteção individual (EPIs) e em local
								inadequado, trabalhadora em posição perigosa no preparo de
								alimentação, monta-carga sem proteção oferecendo risco de
								acidente grave e ralo de esgoto sem vedação em unidade de
								cuidados intensivos.</p>

							<p>O SINDISAÚDE acompanhou a ação por meio de sua diretora de
								Finanças, Maria Tedesco.</p>

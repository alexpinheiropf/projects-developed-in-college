							<p>FEESSERS PARTICIPA COM A CUT E DEMAIS CENTRAIS SINDICAIS,
								PARLAMENTARES E ENTIDADES</p>

							<p>A diretoria da FEESSERS participou no dia 24/02 do Lançamento
								da Frente Gaúcha em Defesa da Previdência Pública na Assembléia
								Legislativa do RS. O presidente da Federação esteve no palco do
								evento, representando a categoria. A presidente do SINDISAÚDE,
								Terezinha Perissinotto, e a diretora Marta Ribeiro também
								participaram do evento.</p>

							<p>Carta</p>

							<p>No final do encontro, as lideranças aprovaram uma carta em que
								condenam o desmonte dos direitos dos trabalhadores. O documento,
								que será enviado ao Congresso, afirma que “o fortalecimento da
								Previdência Social passa pelo combate à sonegação, às
								desonerações e às renúncias e pelo fim das manobras fiscais no
								orçamento”.</p>

							<p>O movimento, que nasceu com o propósito de combater a proposta
								de Reforma da Previdência em tramitação no Congresso, reúne
								parlamentares de diversos partidos, juristas, prefeitos,
								federações e confederações de trabalhadores rurais e urbanos,
								sindicatos de servidores públicos e de trabalhadores da
								iniciativa privada, movimentos sociais, associações de
								profissionais liberais e entidades da sociedade civil, como a
								OAB e Associação dos Auditores Fiscais da Receita Federal.</p>

							<p>Mobilização</p>

							<p>A Frente Gaúcha pretende desencadear uma série de ações
								unificadas no Estado para denunciar o caráter da proposta do
								governo e pressionar a bancada federal do Estado a rejeitar o
								projeto.</p>

							<p>A ideia é promover manifestações de ruas, como as que
								ocorreram nos municípios de Santa Rosa e Santa Cruz do Sul,
								reunindo milhares de pessoas, e formar pequenos comitês nos
								municípios, como os contra a carestia e pela libertação do líder
								sul-africano Nelson Mandela, que funcionaram no Brasil nos anos
								de 1970 e 1980.</p>

							<p>Além disso, a Frente pretende estimular as Câmaras de
								Vereadores do RS a enviarem moções de repúdio à PEC para o
								Congresso Nacional. “O governo Temer criou o projeto, mas são os
								deputados e senadores que irão aprovar ou rejeitar este
								verdadeiro golpe contra os trabalhadores” , avaliou o
								coordenador da Frente Parlamentar Gaúcha em Defesa da
								Previdência Social Rural e Urbana, Altemir Tortelli (PT).</p>


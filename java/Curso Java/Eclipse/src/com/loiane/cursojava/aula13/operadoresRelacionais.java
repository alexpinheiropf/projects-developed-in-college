package com.loiane.cursojava.aula13;

public class operadoresRelacionais {

	public static void main(String[] args) {
		int valor1 = 1;
		int valor2 = 2;
		
		System.out.println("Valor 1 = valor2: " + (valor1 == valor2));
		System.out.println("Valor 1 != valor2: " + (valor1 != valor2));
		System.out.println("Valor 1 > valor2: " + (valor1 > valor2));
		System.out.println("Valor 1 >= valor2: " + (valor1 >= valor2));
		System.out.println("Valor 1 < valor2: " + (valor1 < valor2));
		System.out.println("Valor 1 <= valor2: " + (valor1 <= valor2));
	}

}
